;============================ 扩展知识 ==========================================
;
; 【平坦内存模型（Flat Memory Model）】
;   本MBR采用平坦内存模型，GDT中只需要2个有效描述符（代码段+数据段），
;   两者的基地址均为0，界限均为0xFFFFF（4KB粒度 = 4GB覆盖整个地址空间）。
;   - 极大简化编程：无需在多个段之间切换，所有段寄存器指向同一个平坦空间
;   - 所有线性地址 = 偏移地址（因为基地址为0）
;   - 段机制仅用于满足保护模式的基本要求，实际的内存保护由分页机制完成
;   - 这是现代操作系统（Linux/Windows）通用的内存模型
;
; 【双映射策略（Dual Mapping）】
;   在开启分页时，将物理地址0x00000000同时映射到两个线性地址：
;     ① 0x00000000 → 物理0x00000000（恒等映射，临时过渡用）
;     ② 0x80000000 → 物理0x00000000（高半核映射，内核永久驻留）
;   为什么需要恒等映射？
;     开启分页的瞬间（mov cr0,eax），下一条指令的EIP仍在低地址空间。
;     如果没有0x00000000的映射，CPU会立即产生缺页异常。
;     之后跳转到0x80000000+以上的内核代码后，可安全移除低端映射。
;
; 【add esp,0x80000000 —— 堆栈指针重定位】
;   开启分页后，内核的所有数据（包括堆栈）都必须通过高半地址0x80000000+访问。
;   如果不调整ESP，堆栈仍指向低地址空间，将来加载用户任务清除低端页表时，
;   内核堆栈会立即失效，产生极难调试的错误。
;
; 【sgdt/lgdt 地址修正】
;   GDTR中保存的是GDT的线性地址。开启分页后，所有内存访问都经过分页转换。
;   因此必须把GDTR中的GDT基地址从物理地址改为对应的高半线性地址（+0x80000000），
;   否则后续对GDT的访问将因地址不正确而失败。
;
;===============================================================================

         core_base_address equ 0x00040000   ;常数，内核加载的起始物理地址（256KB处）
         core_start_sector equ 0x00000001   ;常数，内核在硬盘上的起始逻辑扇区号（紧跟MBR之后）

;===============================================================================
SECTION  mbr  vstart=0x00007c00            ;MBR被BIOS加载到0x7C00处执行

	;设置堆栈段和堆栈指针，利用CS=0x0000的特性
	mov ax,cs                          ;CS=0x0000（BIOS跳转到0x0000:0x7C00）
	mov ss,ax                          ;SS=0x0000，与CS相同
	mov sp,0x7c00                      ;堆栈从0x7C00向下增长（不会覆盖代码）

	;计算GDT所在的逻辑段地址（实模式下需要段:偏移形式访问）
	;pgdt+2处存放GDT的32位物理地址，需要拆分为16位段地址和偏移
	mov eax,[cs:pgdt+0x02]             ;取出GDT的32位物理地址（0x00008000）
	xor edx,edx			   ;清零EDX，准备做除法
	mov ebx,16
	div ebx                            ;EAX/16 → EAX=段地址, EDX=段内偏移

	mov ds,eax                         ;DS=GDT所在的段地址，以便写入描述符
	mov ebx,edx                        ;EBX=GDT在段内的起始偏移

	;----------在内存中构建GDT（全局描述符表）----------
	;平坦模型只需2个有效描述符：#1代码段 + #2数据段
	;#0描述符必须为空（处理器规定），无需显式写入（内存已清零）

	;创建#1描述符：平坦代码段（覆盖0-4GB，DPL=0，可执行/可读）
	mov dword [ebx+0x08],0x0000ffff    ;基地址=0，界限=0xFFFFF（低16位+低16位基址）
	mov dword [ebx+0x0c],0x00cf9800    ;G=1(4KB粒度),D=1(32位),P=1,DPL=00,TYPE=1000(只执行)
	;|----------------------------------------------------------------------------|
	;|                |  |  |  |   |  LIMIT |  |     |  |        |                |
	;|    BASE:31-24  |G |DB|L |AVL| 19--16 |P |DPL  |S |  TYPE  |    BASE:23-16  |
	;|----------------|--|--|--|---|--------|--|-----|--|--------|----------------|
	;|    31--24      |23|22|21|20 | 19--16 |15|14-13|12|  11-8  |      7--0      |   4
	;|----------------|--|--|--|---|--------|--|-----|--|--------|----------------|
	;|    00000000    |1 |1 |0 |0  |  1111  |1 |  00 |1 |  1000  |    00000000    |
	;|--------------------------------------|-------------------------------------|
	;|           00CF                       |              9800                   |

	;31-------------------------------------15------------------------------------0
	;|--------------------------------------|-------------------------------------|
	;|       SEGMENT BASE 15-0              |          SEGMENT limit 15-0         |
	;|--------------------------------------|-------------------------------------|   0
	;|           0000                       |              FFFF                   |
	;|--------------------------------------|-------------------------------------|

	;创建#2描述符：平坦数据/堆栈段（覆盖0-4GB，DPL=0，可读写）
	mov dword [ebx+0x10],0x0000ffff    ;基地址=0，界限=0xFFFFF（低16位+低16位基址）
	mov dword [ebx+0x14],0x00cf9200    ;G=1(4KB粒度),D=1(32位),P=1,DPL=00,TYPE=0010(可读写)
	;|----------------------------------------------------------------------------|
	;|                |  |  |  |   |  LIMIT |  |     |  |        |                |
	;|    BASE:31-24  |G |DB|L |AVL| 19--16 |P |DPL  |S |  TYPE  |    BASE:23-16  |
	;|----------------|--|--|--|---|--------|--|-----|--|--------|----------------|
	;|    31--24      |23|22|21|20 | 19--16 |15|14-13|12|  11-8  |      7--0      |
	;|----------------|--|--|--|---|--------|--|-----|--|--------|----------------|
	;|    00000000    |1 |1 |0 |0  |  1111  |1 |  00 |1 |  0010  |    00000000    |
	;|--------------------------------------|-------------------------------------|
	;|           00CF                       |              9200                   |

	;|--------------------------------------|-------------------------------------|
	;|       SEGMENT BASE 15-0              |          SEGMENT limit 15-0         |
	;|--------------------------------------|-------------------------------------|
	;|           0000                       |              FFFF                   |
	;|--------------------------------------|-------------------------------------|

	;初始化GDTR：共3个描述符（#0空+#1代码+#2数据），每个8字节，共24字节
	mov word [cs:pgdt],23             ;GDT界限 = 总字节数-1 = 24-1 = 23

	;将GDT的基地址和界限加载到GDTR寄存器（48位：16位界限+32位基地址）
	lgdt [cs:pgdt]

	;打开A20地址线（突破1MB寻址限制，访问完整4GB地址空间）
	;端口0x92是快速A20门控（Fast A20 Gate），bit1控制A20
	in al,0x92                         ;从南桥芯片的系统控制端口读取当前值
	or al,0000_0010B                   ;置位bit1，开启A20
	out 0x92,al                        ;写回端口，A20地址线打开

	cli                                ;关闭中断（IDT尚未建立，中断会导致崩溃）

	;开启保护模式：设置CR0寄存器的PE位（bit0）
	;31                23                15                7                 0
	;╔═════════════════╪═════════════════╪════════╦════════╪═════════════════╗
	;║                                            ║                          ║
	;║        PAGE DIRECTORY BASE REGISTER (PDBR) ║ RESERVED                 ║CR3
	;╟────────────────────────────────────────────╨──────────────────────────╢
	;║                                                                       ║
	;║                  PAGE FAULT LINEAR ADDRESS                            ║CR2
	;╟───────────────────────────────────────────────────────────────────────╢
	;║                                                                       ║
	;║                                RESERVED                               ║CR1
	;╟─┬───────────────────────────────────────────────────────────┬─┬─┬─┬─┬─╢
	;║P│                                                           │E│T│E│M│P║
	;║G│                   RESERVED                                │T│S│M│P│E║CR0
	;╚═╧═══════════════╪═════════════════╪═════════════════╪═══════╧═╧═╧═╧═╧═╝
	mov eax,cr0                        ;读取CR0寄存器
	or eax,1                           ;置位PE位（bit0），进入保护模式
	mov cr0,eax                        ;写回CR0，处理器立即切换到保护模式

	;进入保护模式后，必须立即执行远跳转以：
	;  ① 清空CPU流水线中残留的16位指令译码
	;  ② 加载CS段选择子（0x0008=#1代码段描述符）
	jmp dword 0x0008:flush		;段选择子0x0008：索引=1, TI=0(GDT), RPL=00
	                            	;清流水线并串行化处理器
	;|    描述符索引               |TI |  DPL |
	;|------------------------|---|------|
	;|       15--3            | 2 | 1--0 |
	;|------------------------|---|------|
	;|      0000 0000 0000 1  | 0 |  00  |
	;|------------------------|---|------|
	;|             0008                  |

	[bits 32]                          ;以下为32位保护模式代码
flush:
	;所有数据段/堆栈段都使用#2描述符（0x0010）的平坦4GB空间
	mov eax,0x0000_0010                ;#2数据段选择子：索引=2, TI=0, RPL=00

	mov ds,eax                         ;数据段 → 平坦4GB
	mov es,eax                         ;附加段 → 平坦4GB
	mov fs,eax                         ;附加段 → 平坦4GB
	mov gs,eax                         ;附加段 → 平坦4GB
	mov ss,eax                         ;堆栈段 → 平坦4GB

	mov esp,0x7000                     ;设置堆栈指针（临时，位于低端物理内存）

	;=== 加载内核程序到物理内存 ===
	mov eax,core_start_sector	   ;内核起始扇区号（LBA=1）
	mov edi,core_base_address	   ;内核加载目标地址（0x00040000）
	mov ebx,edi                        ;EBX=目标缓冲区地址（read_hard_disk_0需要）

	call read_hard_disk_0              ;先读取第一个扇区（包含内核头部信息）

	;根据内核头部的总长度字段，计算还需读取多少个扇区
	mov eax,[edi]                      ;[0x00040000]=内核程序总字节数
	xor edx,edx                        ;清零高32位，准备32位除法
	mov ecx,512                        ;每扇区512字节
	div ecx			    	   ;EAX=完整扇区数, EDX=剩余字节数

	or edx,edx			   ;检查是否有余数（余数为0时ZF=1）
	jnz @1                             ;有余数→实际需要的扇区数=商+1，不减
	dec eax                            ;无余数→已读1扇区，剩余扇区数=商-1

@1:
	or eax,eax                         ;内核≤512字节？（EAX=0则无需再读）
	jz pge                             ;直接跳到分页设置

	;循环读取剩余扇区到连续物理内存
	mov ecx,eax                        ;ECX=剩余要读的扇区数（LOOP计数器）
	mov eax,core_start_sector
	inc eax                            ;从第2个扇区（LBA=2）开始接着读

	@2:
		call read_hard_disk_0      ;读取一个扇区，EBX自动后移512字节
		inc eax                    ;下一个逻辑扇区号
	loop @2                            ;ECX-=1，非零则继续

;============================= 设置分页机制 ====================================
pge:
	;--- 第1步：创建页目录表（PDT）---
	mov ebx,0x00020000                 ;页目录表放在物理地址0x20000处

	;页目录最后一项（第1023项，偏移4092）指向页目录自身
	;这样通过线性地址0xFFFFF000即可访问页目录本身（自映射技巧）
	mov dword [ebx+4092],0x00020003    ;物理地址0x20000 + 属性(P=1,R/W=1,U/S=0)

	mov edx,0x00021003                 ;页表物理地址0x21000 + 属性(P=1,R/W=1,U/S=0)

	;【恒等映射】线性地址0x00000000对应的页目录项（索引0，偏移0x000）
	mov [ebx+0x000],edx                ;临时映射，用于开启分页后的平滑过渡

	;【高半核映射】线性地址0x80000000对应的页目录项（索引512，偏移0x800）
	;0x80000000>>22=512, 512*4=0x800
	mov [ebx+0x800],edx                ;内核永久驻留在0x80000000+

	;--- 第2步：创建页表（映射低端1MB物理内存 = 256个4KB页）---
	mov ebx,0x00021000                 ;页表放在物理地址0x21000处

	xor eax,eax                        ;起始物理页地址=0x00000000
	xor esi,esi                        ;页表项索引=0

	.b1:
		mov edx,eax
		or edx,0x00000003          ;设置属性：P=1(存在), R/W=1(可写), U/S=0(特权)
		mov [ebx+esi*4],edx        ;将页表项写入页表
		add eax,0x1000             ;下一个物理页（每页4KB=0x1000字节）
		inc esi
		cmp esi,256                ;256个页=256*4KB=1MB，仅映射低端1MB
	jl .b1

	;--- 第3步：加载CR3并开启分页 ---
	mov eax,0x00020000                 ;页目录表物理地址，PCD=0(允许缓存),PWT=0(回写)
	mov cr3,eax                        ;CR3(PDBR)指向页目录表

	;【关键】修正GDTR中的GDT基地址：从物理地址转为高半线性地址
	;开启分页后，处理器用线性地址访问GDT，必须映射到0x80000000+
	sgdt [pgdt]                        ;将当前GDTR内容保存到pgdt
	mov ebx,[pgdt+2]                   ;取出GDT原始物理基地址
	add dword [pgdt+2],0x80000000      ;加上0x80000000得到高半线性地址
	lgdt [pgdt]                        ;重新加载修正后的GDTR

	;设置CR0的PG位（bit31），正式开启分页
	mov eax,cr0
	or eax,0x80000000                  ;置位PG位（bit31）
	mov cr0,eax                        ;分页机制立即生效！

	;【关键】将堆栈指针重定位到高半核空间
	;不做此步，后续清除低端映射时，堆栈将无法访问
	add esp,0x80000000                 ;ESP: 0x7000 → 0x80007000

	;跳转到内核入口点（位于高半地址空间0x80040000+）
	jmp [0x80040004]  	 	   ;间接跳转，[0x80040004]存放core_entry的值
       

;-------------------------------------------------------------------------------
read_hard_disk_0:                       ;从硬盘读取一个逻辑扇区（LBA模式）
                                        ;输入：EAX=逻辑扇区号（LBA28）
                                        ;      DS:EBX=目标缓冲区地址
	                            	;返回：EBX=EBX+512（指向下一个缓冲区位置）
	push eax		        ;保护寄存器（调用者可能还需要EAX的值）
	push ecx
	push edx
		;第1步：设置要读取的扇区数=1
		push eax		    ;暂存EAX（后面写端口会修改AL）
			mov dx,0x1f2       ;扇区计数端口
			mov al,1           ;读1个扇区
			out dx,al          ;写入扇区计数
		pop eax                    ;恢复EAX中的完整LBA地址

		;第2步：将28位LBA地址分4次写入端口0x1f3-0x1f6
		inc dx                             ;0x1f3：LBA低8位
		out dx,al                          ;写入LBA[7:0]

		inc dx                             ;0x1f4：LBA中低8位
		mov cl,8
		shr eax,cl                         ;右移8位
		out dx,al                          ;写入LBA[15:8]

		inc dx                             ;0x1f5：LBA中高8位
		shr eax,cl
		out dx,al                          ;写入LBA[23:16]

		inc dx                             ;0x1f6：高4位+驱动器/模式选择
		shr eax,cl
		or al,0xe0                         ;0xE0=1110_0000:LBA模式,主硬盘,LBA[27:24]
		out dx,al

		;第3步：发送读命令
		inc dx                             ;0x1f7：命令/状态端口
		mov al,0x20                        ;0x20=READ SECTORS命令
		out dx,al

		;第4步：轮询等待硬盘就绪
		.waits:
			in al,dx                   ;读取状态寄存器
			and al,0x88		    ;保留BSY(bit7)和DRQ(bit3)
			cmp al,0x08		    ;BSY=0且DRQ=1表示数据就绪
		jnz .waits                         ;未就绪则继续等待

		;第5步：从数据端口读取512字节（256个字）
		mov ecx,256                        ;每次in读2字节(一个字)，共读256次=512字节
		mov dx,0x1f0			   ;数据端口

		.readw:
			in ax,dx               ;从数据端口读一个字（16位）
			mov [ebx],ax           ;存入缓冲区
			add ebx,2              ;缓冲区指针后移2字节
		loop .readw

	pop edx
	pop ecx
	pop eax

	ret

;-------------------------------------------------------------------------------
	pgdt      dw 0			;GDT界限（16位），由代码动态填写
	          dd 0x00008000		;GDT物理基地址（32位），位于0x8000处
;-------------------------------------------------------------------------------
	times 510-($-$$) db 0              ;用0填充至510字节
                  db 0x55,0xaa                 ;MBR魔数签名（BIOS据此识别有效引导扇区）
         ;===============================================================================
         ; 扩展知识（Extended Knowledge）
         ;===============================================================================
         ;
         ; 【1】jmp near start 模式 —— 为什么要跳过数据区？
         ;     MBR 被 BIOS 加载到 0x7C00 后，CPU 从第一条指令开始顺序执行。
         ;     如果数据（mytext / number）放在代码之前，CPU 会把数据当作指令来
         ;     译码执行，导致不可预期的行为。因此用 jmp near start 跳过数据区，
         ;     直接进入真正的可执行代码。"near" 表示段内跳转（16 位偏移），
         ;     机器码占 3 字节（0xE9 + 2 字节偏移量）。
         ;
         ; 【2】movsw / movsb —— 字符串搬运指令
         ;     movsb：将 DS:SI 指向的 **1 字节** 复制到 ES:DI，然后 SI 和 DI
         ;            各自根据方向标志 DF 自动 ±1。
         ;     movsw：将 DS:SI 指向的 **1 个字（2 字节）** 复制到 ES:DI，然后
         ;            SI 和 DI 各自根据方向标志 DF 自动 ±2。
         ;     方向标志 DF：
         ;       cld —— 清除 DF（DF=0），地址递增方向（低→高），最常用。
         ;       std —— 设置 DF（DF=1），地址递减方向（高→低）。
         ;     配合 rep 前缀：rep movsw 会重复执行 movsw 共 CX 次，每次 CX-1，
         ;     直到 CX=0，实现批量内存拷贝。
         ;
         ; 【3】loop 指令
         ;     每次执行时先将 CX 减 1，若 CX ≠ 0 则跳转到目标标号继续循环；
         ;     若 CX = 0 则顺序执行下一条指令。
         ;     等价伪代码：CX = CX - 1; if (CX != 0) goto label;
         ;     注意：loop 只检测 CX，不影响任何标志位。
         ;
         ; 【4】jns 指令 —— 符号标志跳转
         ;     jns = Jump if Not Sign，即当符号标志 SF=0（结果为非负数）时跳转。
         ;     本程序用 dec si 后 jns show：si 从 4 递减到 0 时 SF=0，继续循环；
         ;     当 si 从 0 减为 -1（0xFFFF）时，结果的最高位为 1，SF=1，不跳转，
         ;     循环结束。因此共循环 5 次（si=4,3,2,1,0）。
         ;
         ; 【5】times 伪指令 —— 填充 MBR 到 510 字节
         ;     times 510-($-$$) db 0
         ;     $ 表示当前汇编地址，$$ 表示当前节（section）的起始地址。
         ;     ($-$$) 即已使用的字节数，510-($-$$) 为需要填充的零字节数。
         ;     填充后再追加 0x55, 0xAA（共 512 字节），构成合法的 MBR 引导签名，
         ;     BIOS 检测到最后两字节为 0x55AA 才会认为这是有效的引导扇区。
         ;
         ; 【6】段地址表示法的区别：0x7C0:offset vs 0x0000:0x7C00+offset
         ;     两者指向相同的物理地址：物理地址 = 段基址×16 + 偏移。
         ;       0x07C0:0x0000 → 0x07C0×16 + 0x0000 = 0x7C00
         ;       0x0000:0x7C00 → 0x0000×16 + 0x7C00 = 0x7C00
         ;     本程序使用 mov ax,0x7C0 / mov ds,ax 的方式（段基址 = 0x7C0），
         ;     这样 mytext、number 等标号的偏移量可以直接使用汇编器生成的
         ;     相对于文件起始位置的小偏移值（从 0 开始），更简洁直观。
         ;     若使用 DS=0x0000，则所有数据访问都要加上 0x7C00 的基偏移，
         ;     汇编器默认的标号偏移将不能直接使用，需要额外调整（org 0x7C00）。
         ;
         ;===============================================================================

         jmp near start                 ;跳过下面的数据区，直接进入代码段执行（见扩展知识【1】）

  ;--- 数据区：放在 jmp 之后、代码之前，避免 CPU 将数据误当指令执行 ---
  mytext db 'L',0x07,'a',0x07,'b',0x07,'e',0x07,'l',0x07,' ',0x07,'o',0x07,\
            'f',0x07,'f',0x07,'s',0x07,'e',0x07,'t',0x07,':',0x07
            ;每个字符后跟属性字节 0x07（黑底白字），共 13 个"字符+属性"对
  number db 0,0,0,0,0                  ;5 字节缓冲区，用于存放数字的各个十进制位

  ;=========================== 代码从此处开始 ===========================
  start:
         mov ax,0x7c0                  ;DS = 0x7C0，物理基址 = 0x7C0×16 = 0x7C00
         mov ds,ax                     ;设置数据段寄存器，使标号偏移从 0 开始即可寻址

         mov ax,0xb800                 ;ES = 0xB800，指向显存文本模式起始地址
         mov es,ax                     ;后续用 ES:DI 写入显存

         ;--- 使用 movsw 将字符串从 DS:SI 批量拷贝到 ES:DI（见扩展知识【2】）---
         mov si,mytext                 ;SI 指向源数据（DS 段内 mytext 的偏移）
         mov di,160*8                  ;DI 指向显存第 8 行起始位置（每行 160 字节 = 80 列×2 字节）
         mov cx,(number-mytext)/2      ;CX = 拷贝次数，(number-mytext) 是字节数，除以 2 得字数 = 13
         cld                           ;清除方向标志 DF=0，movsw 后 SI/DI 自动递增
         rep movsw                     ;重复执行 movsw 共 CX(13) 次，将整个字符串搬入显存

         ;--- 准备要显示的数值 ---
         ;得到标号所代表的偏移地址
         ;mov ax,number                ;（注释掉的原方案：显示 number 标号的偏移地址）
         mov ax,1234                   ;当前演示值：将 1234 分解为各个十进制位并显示

         ;--- 用循环将 AX 中的数值逐位分解（除 10 取余法）（见扩展知识【3】）---
         ;计算各个数位
         ;mov bx,ax
         mov bx,0                      ;BX 用作 number 缓冲区的索引，从 [DS:0] 即 number[0] 开始
         mov cx,5                      ;循环 5 次（分解出 5 位十进制数，不足高位自动为 0）
         mov si,10                     ;除数 = 10
  digit:
         xor dx,dx                     ;DX:AX 组成 32 位被除数，高 16 位 DX 清零
         div si                        ;DX:AX ÷ SI(10)，商→AX，余数→DX（DL 即当前最低位）
         mov [bx],dl                   ;将余数（当前位的数字 0-9）存入 number 缓冲区
         inc bx                        ;索引后移，指向下一个存储位置
         loop digit                    ;CX = CX-1，若 CX≠0 继续循环（见扩展知识【3】）

         ;--- 从高位到低位显示各数位（见扩展知识【4】关于 jns）---
         ;显示各个数位
         ;mov bx,number                ;（注释掉的原方案：用 number 标号地址）
         mov bx, 0                     ;BX = number 缓冲区起始偏移（DS 段内）
         mov si,4                      ;SI = 4，从 number[4]（最高位）开始向 number[0]（最低位）遍历
   show:
         mov al,[bx+si]               ;取出 number[bx+si] 中存放的数字（0-9）
         add al,0x30                   ;加 0x30 转换为 ASCII 码（'0'=0x30 ... '9'=0x39）
         mov ah,0x04                   ;属性字节：黑底红字（0x04）
         mov [es:di],ax               ;将"字符+属性"写入显存（低字节=字符，高字节=属性）
         add di,2                      ;显存指针后移 2 字节，指向下一个字符位置
         dec si                        ;SI 递减，从高位向低位遍历
         jns show                      ;SF=0（SI≥0）时继续循环；SI 减为 -1 时 SF=1，退出（共 5 次）

         mov word [es:di],0x0744       ;在数字末尾显示字符 'D'（ASCII 0x44），属性 0x07（黑底白字）

         jmp near $                    ;无限循环：$ 代表当前指令地址，跳转到自身，程序停在此处

  ;--- 填充剩余空间并添加 MBR 引导签名（见扩展知识【5】）---
  times 510-($-$$) db 0               ;用 0x00 填充至第 510 字节
                   db 0x55,0xaa        ;第 511-512 字节：MBR 有效引导签名 0x55AA
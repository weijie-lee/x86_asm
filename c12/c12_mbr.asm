	;=============================================================================
	;                   扩展知识：4GB平坦内存模型与代码段别名
	;=============================================================================
	;
	; 【一、4GB平坦内存模型（Flat Memory Model）】
	;   在保护模式下，通过设置段描述符可以创建一个覆盖整个4GB地址空间的段：
	;     - 基地址（Base）= 0x00000000
	;     - 段界限（Limit）= 0xFFFFF（20位全1）
	;     - G=1（粒度为4KB）
	;   实际可访问范围 = (0xFFFFF + 1) × 4KB = 1M × 4KB = 4GB
	;   这就是所谓的"平坦模型"——整个物理地址空间被映射为一个连续的段，
	;   偏移量就等于线性地址（物理地址），简化了内存访问。
	;   对应描述符低32位=0x0000FFFF，高32位=0x00CF9200（数据段）或0x00CF9800（代码段）
	;
	; 【二、代码段别名（Code Segment Aliasing）】
	;   在保护模式下，代码段（TYPE的X位=1）是只读/可执行的，不能通过代码段描述符写入数据。
	;   但有时候需要修改代码段中的数据（如自修改代码、初始化数据等），怎么办？
	;   解决方案——代码段别名（Alias）：
	;     创建一个数据段描述符，其基地址和段界限与目标代码段完全相同，
	;     但TYPE改为数据段属性（如0010=可读可写），这样就可以通过这个"别名"段
	;     对同一块内存区域进行读写操作。
	;   本程序中：
	;     #2号描述符：代码段（TYPE=1000，只执行），基地址=0x7C00，512字节
	;     #3号描述符：#2的别名（TYPE=0010，可读可写数据段），基地址=0x7C00，512字节
	;     两个描述符指向同一段内存，但权限不同。
	;
	; 【三、冒泡排序在汇编中的实现】
	;   冒泡排序需要双层嵌套循环，但x86的loop指令只使用ECX作为计数器。
	;   解决方法：外层循环开始时push ecx保存外层计数，内层循环结束后pop ecx恢复。
	;   排序流程：
	;     外层循环（ECX = 串长度-1 次）：
	;       push ecx        ;保存外层计数器
	;       内层循环（ECX = 当前外层计数值 次）：
	;         比较相邻两个字节
	;         如果顺序不对，用 xchg 交换
	;         loop 内层循环
	;       pop ecx          ;恢复外层计数器
	;       loop 外层循环
	;
	; 【四、xchg指令（Exchange，交换）】
	;   xchg op1, op2 ：原子性地交换两个操作数的值。
	;   例如：xchg al, ah 交换AL和AH的值。
	;   当操作数之一为内存时，处理器会自动加LOCK前缀，保证操作的原子性。
	;   这在多处理器环境中非常有用（如实现自旋锁）。
	;
	; 【五、32位寻址模式】
	;   保护模式下可以使用32位寄存器作为地址偏移，寻址能力大大增强：
	;     - [ebx]：直接使用32位寄存器
	;     - [string+ebx]：基地址（标号/立即数）+ 32位寄存器偏移
	;     - [es:0xb8000+ebx*2]：段超越 + 基地址 + 变址×比例因子（1/2/4/8）
	;   这种灵活的寻址方式是实模式16位寻址无法比拟的。
	;
	; 【六、GDT #0号描述符必须为空（Null Descriptor）】
	;   这是处理器的硬性要求。GDT的第0个条目必须全部为零。
	;   如果将选择子0x0000加载到段寄存器（DS/ES/FS/GS），不会立即触发异常，
	;   但当通过该段寄存器访问内存时，处理器会产生#GP（一般保护异常）。
	;   这是一种保护机制：防止程序使用未初始化的段寄存器访问内存。
	;   注意：CS和SS不能加载空选择子，否则立即产生#GP。
	;
	;=============================================================================

	;===== 第一步：初始化实模式下的堆栈段和栈指针 =====
	;BIOS将MBR加载到0x0000:0x7C00，此时CS=0x0000
	;将SS初始化为CS值（0x0000），SP=0x7C00，栈空间范围0x7BFF~0x0000
	mov eax,cs
	mov ss,eax                         ;SS=CS=0x0000，堆栈段与代码段同基址
	mov sp,0x7c00                      ;SP=0x7C00，栈从此处向低地址增长

	;===== 第二步：计算GDT所在的逻辑段地址 =====
	;GDT位于物理地址0x7E00处，需转换为"段地址:偏移"形式（实模式寻址需要）
	mov eax,[cs:pgdt+0x7c00+0x02]      ;取GDT的32位线性基地址（0x00007E00）
	xor edx,edx                        ;清零EDX，准备做32位除法（EDX:EAX为被除数）
	mov ebx,16
	;执行除法：EDX:EAX / EBX，商在EAX（段地址），余数在EDX（段内偏移）
	;注意：实模式下只使用低16位有效值
	div ebx                            ;分解32位物理地址为16位逻辑地址

	mov ds,eax                         ;DS=段地址，指向GDT所在内存段的起始位置
	mov ebx,edx                        ;EBX=段内偏移，作为写入描述符的基准地址

	;===== 第三步：在GDT中创建段描述符（共5个：#0~#4） =====

	;--- 创建0#描述符（空描述符，处理器强制要求GDT首项为全零） ---
	;加载空选择子到DS/ES等并访问内存会触发#GP异常——防止使用未初始化的段寄存器
	mov dword [ebx+0x00],0x00000000    ;低32位全零
	mov dword [ebx+0x04],0x00000000    ;高32位全零

	;--- 创建1#描述符：4GB平坦数据段（覆盖整个线性地址空间 0~4GB） ---
	;基地址=0，段界限=0xFFFFF，G=1（4KB粒度），实际范围=4GB
	;TYPE=0010（可读可写数据段），用于通过ES/FS/GS访问任意物理地址
	mov dword [ebx+0x08],0x0000ffff    ;低32位：基地址低16位=0x0000，段界限低16位=0xFFFF
	mov dword [ebx+0x0c],0x00cf9200    ;高32位：G=1,D/B=1,Limit高4位=F,P=1,DPL=00,S=1,TYPE=0010

	;--- 创建#2描述符：保护模式下的初始代码段描述符 ---
	;基地址=0x00007C00（MBR加载位置），段界限=0x001FF（512字节）
	;TYPE=1000（只可执行代码段），G=0（字节粒度）
	;进入保护模式后，处理器从该段开始执行
	mov dword [ebx+0x10],0x7c0001ff    ;低32位：基地址低16位=0x7C00，界限低16位=0x01FF
	mov dword [ebx+0x14],0x00409800    ;高32位：G=0,D/B=1,P=1,DPL=00,S=1,TYPE=1000（只执行）
	;|----------------------------------------------------------------------------|
	;|                |  |  |  |   |  LIMIT |  |     |  |        |                |
	;|    BASE:31-24  |G |DB|L |AVL| 19--16 |P |DPL  |S |  TYPE  |    BASE:23-16  |
	;|----------------|--|--|--|---|--------|--|-----|--|--------|----------------|
	;|    31--24      |23|22|21|20 | 19--16 |15|14-13|12|  11-8  |      7--0      |
	;|----------------|--|--|--|---|--------|--|-----|--|--------|----------------|
	;|    00000000    |0 |1 |0 |0  |  0000  |1 |  00 |1 |  1000  |    00000000    |
	;|--------------------------------------|-------------------------------------|
	;|           0040                       |              9800                   |
	;
	;|--------------------------------------|-------------------------------------|
	;|       SEGMENT BASE 15-0              |          SEGMENT limit 15-0         |
	;|--------------------------------------|-------------------------------------|
	;|           7c00                       |              01ff                   |
	;|--------------------------------------|-------------------------------------|

	;--- 创建#3描述符：#2代码段的别名描述符（Alias Descriptor） ---
	;与#2指向完全相同的内存区域（基地址=0x7C00，512字节），但权限不同
	;TYPE=0010（可读可写数据段），这样就可以通过DS访问并修改代码段中的数据
	;保护模式下代码段不可写——别名机制是唯一合法的修改方式
	mov dword [ebx+0x18],0x7c0001ff    ;低32位：与#2完全相同
	mov dword [ebx+0x1c],0x00409200    ;高32位：G=0,D/B=1,P=1,DPL=00,S=1,TYPE=0010（可读可写）
	;|----------------------------------------------------------------------------|
	;|                |  |  |  |   |  LIMIT |  |     |  |        |                |
	;|    BASE:31-24  |G |DB|L |AVL| 19--16 |P |DPL  |S |  TYPE  |    BASE:23-16  |
	;|----------------|--|--|--|---|--------|--|-----|--|--------|----------------|
	;|    31--24      |23|22|21|20 | 19--16 |15|14-13|12|  11-8  |      7--0      |
	;|----------------|--|--|--|---|--------|--|-----|--|--------|----------------|
	;|    00000000    |0 |1 |0 |0  |  0000  |1 |  00 |1 |  0010  |    00000000    |
	;|--------------------------------------|-------------------------------------|
	;|           0040                       |              9200                   |
	;
	;|--------------------------------------|-------------------------------------|
	;|       SEGMENT BASE 15-0              |          SEGMENT limit 15-0         |
	;|--------------------------------------|-------------------------------------|
	;|           7C00                       |              01FF                   |
	;|--------------------------------------|-------------------------------------|

	;--- 创建#4描述符：堆栈段描述符 ---
	;基地址=0x00007C00，段界限=0xFFFFE，G=1（4KB粒度）
	;TYPE=0110（向下扩展、可读可写的数据段——堆栈段）
	mov dword [ebx+0x20],0x7c00fffe    ;低32位：基地址低16位=0x7C00，界限低16位=0xFFFE
	mov dword [ebx+0x24],0x00cf9600    ;高32位：G=1,D/B=1,Limit高4位=F,P=1,DPL=00,S=1,TYPE=0110
	;|----------------------------------------------------------------------------|
	;|                |  |  |  |   |  LIMIT |  |     |  |        |                |
	;|    BASE:31-24  |G |DB|L |AVL| 19--16 |P |DPL  |S |  TYPE  |    BASE:23-16  |
	;|----------------|--|--|--|---|--------|--|-----|--|--------|----------------|
	;|    31--24      |23|22|21|20 | 19--16 |15|14-13|12|  11-8  |      7--0      |
	;|----------------|--|--|--|---|--------|--|-----|--|--------|----------------|
	;|    00000000    |1 |1 |0 |0  |  1111  |1 |  00 |1 |  0110  |    00000000    |
	;|--------------------------------------|-------------------------------------|
	;|           00CF                       |              9600                   |
	;
	;|--------------------------------------|-------------------------------------|
	;|       SEGMENT BASE 15-0              |          SEGMENT limit 15-0         |
	;|--------------------------------------|-------------------------------------|
	;|           7C00                       |              FFFE                   |
	;|--------------------------------------|-------------------------------------|

	;===== 第四步：初始化GDTR寄存器 =====
	;GDT共5个描述符（#0~#4），每个8字节，共40字节
	;GDTR界限值 = 40 - 1 = 39
	mov word [cs:pgdt+0x7c00],39      ;将界限值39写入GDTR数据结构的低2字节

	;将6字节GDTR数据结构（2字节界限 + 4字节GDT基地址）加载到GDTR寄存器
	lgdt [cs:pgdt+0x7c00]

	;===== 第五步：打开A20地址线 =====
	;通过端口0x92的bit 1开启A20，使CPU能访问1MB以上地址空间
	in al,0x92                         ;从南桥芯片的系统控制端口A读取当前值
	or al,0000_0010B                   ;将bit 1置1（开启A20地址线）
	out 0x92,al                        ;写回端口，A20打开

	;===== 第六步：关中断 =====
	;保护模式的IDT（中断描述符表）尚未建立，禁止中断以防止异常
	cli                                ;清除IF标志，禁止可屏蔽中断

	;===== 第七步：设置CR0的PE位，切换到保护模式 =====
	mov eax,cr0                        ;读取CR0当前值
	or eax,1                           ;将bit 0（PE位）置1
	mov cr0,eax                        ;写回CR0——处理器正式进入保护模式

	;===== 第八步：远跳转刷新流水线，进入保护模式代码 =====
	;选择子0x0010 = 索引#2（代码段），TI=0（GDT），RPL=00（Ring 0）
	;|    描述符索引               |TI |  RPL |
	;|------------------------|---|------|
	;|       15--3            | 2 | 1--0 |
	;|------------------------|---|------|
	;|      0000 0000 0001 0  | 0 |  00  |
	;|------------------------|---|------|
	;|             0010                  |
	;偏移地址flush就是标号在段内的偏移量——该段基地址0x7C00正是MBR起始地址
	jmp dword 0x0010:flush             ;远跳转：清空流水线 + 加载CS=#2代码段选择子

	[bits 32]                          ;告诉汇编器以下代码按32位模式编码
flush:
	;===== 进入32位保护模式，初始化各数据段寄存器 =====

	;--- 加载#1号描述符（4GB平坦数据段）到ES/FS/GS ---
	;选择子0x0008 = 索引#1，TI=0（GDT），RPL=00
	;通过ES可以直接用线性地址访问0~4GB的任意位置（偏移=物理地址）
	mov eax,0x0008                     ;加载4GB平坦数据段选择子
	mov es,eax                         ;ES -> 0~4GB全地址空间
	mov fs,eax                         ;FS -> 同上
	mov gs,eax                         ;GS -> 同上

	;--- 加载#3号描述符（代码段别名/可读可写数据段）到DS ---
	;选择子0x0018 = 索引#3，TI=0（GDT），RPL=00
	;DS指向与代码段相同的内存区域（基地址0x7C00），但拥有读写权限
	;这样就可以通过DS读写代码段中定义的字符串数据（如string标号处的数据）
	mov eax,0x0018
	mov ds,eax                         ;DS -> 代码段别名段（基地址0x7C00，可读可写）

	;--- 加载#4号描述符（堆栈段）到SS ---
	;选择子0x0020 = 0000_0000_0010_0_000，索引#4，TI=0，RPL=00
	mov eax,0x0020
	mov ss,eax                         ;SS -> 堆栈段（基地址0x7C00，向下扩展）
	xor esp,esp                        ;ESP=0，栈从基地址+ESP处开始

	;===== 在屏幕第9行显示"P.M. ok" =====
	;通过ES（4GB平坦段）直接用物理地址访问显存
	;文本模式显存起始于0xB8000，每行160字节，第9行偏移=160×8=1280=0x500
	;每个字符占2字节：低字节=ASCII码，高字节=显示属性（0x07=白色字符/黑色背景）
	;此处用dword写入，一次写两个字符（每个dword=4字节=2个字符）
	mov dword [es:0x0b8000+160*8],0x072e0750 ;'P'(0x50)+属性0x07, '.'(0x2e)+属性0x07
	mov dword [es:0x0b8004+160*8],0x072e074d ;'M'(0x4d)+属性0x07, '.'(0x2e)+属性0x07
	mov dword [es:0x0b8008+160*8],0x07200720 ;' '(0x20)+属性0x07, ' '(0x20)+属性0x07
	mov dword [es:0x0b800c+160*8],0x076b076f ;'o'(0x6f)+属性0x07, 'k'(0x6b)+属性0x07

	;===== 冒泡排序：对string处的字符串按ASCII值升序排列 =====
	;外层循环次数 = 字符串长度 - 1（pgdt标号地址 - string标号地址 - 1）
	;冒泡排序原理：每轮将未排序部分中最大值"冒泡"到末尾
	mov ecx,pgdt-string-1              ;ECX=外层循环计数器（遍历次数=串长度-1）
@@1:
	push ecx                           ;保存外层循环计数器（因为内层loop也会修改ECX）
	xor bx,bx                          ;BX=0，作为内层循环的字符索引（可用16位偏移）
@@2:                                       ;--- 内层循环开始 ---
	mov ax,[string+bx]                 ;取相邻两字节：AL=string[bx]，AH=string[bx+1]
	cmp ah,al                          ;比较：AH（高字节）与 AL（低字节）
	jge @@3 			   ;如果AH >= AL（已有序），跳过交换
	                                   ;否则AH < AL（逆序），需要交换
	xchg al,ah                         ;原子交换AL和AH的值（将较小值放到低位）
	mov [string+bx],ax		   ;写回交换后的值——每轮将最大ASCII值冒泡到右端

@@3:
	inc bx                             ;索引加1，指向下一对相邻字符
	loop @@2                           ;ECX减1，不为0则继续内层循环

	pop ecx                            ;恢复外层循环计数器
	loop @@1                           ;ECX减1，不为0则继续外层循环

	;===== 在屏幕第10行显示排序后的字符串 =====
	;使用ES段（4GB平坦数据段）直接用物理地址写入显存
	;显示属性0x06=棕色字符/黑色背景
	mov ecx,pgdt-string                ;ECX=字符串长度（循环计数器）
	xor ebx,ebx			   ;EBX=0，32位偏移地址（32位寻址更灵活）
@@4:					   ;--- 逐字符显示 ---
	mov ah,0x06                        ;AH=显示属性（棕色字符）
	mov al,[string+ebx]                ;AL=当前字符（使用32位基址+偏移寻址：[string+ebx]）
	mov [es:0xb80a0+ebx*2+160*8],ax    ;写入显存：使用比例因子寻址 [es:基址+变址×2]
	                                   ;ebx*2是因为显存中每字符占2字节（字符+属性）
	                                   ;0xb80a0 = 0xb8000+160，即第2行起始（相对原点）
	                                   ;加上160*8偏移到第10行
	inc ebx                            ;字符索引加1
	loop @@4                           ;ECX减1，不为0则继续显示下一个字符

	hlt                                ;停机——程序执行完毕

;-------------------------------------------------------------------------------
	string           db 's0ke4or92xap3fv8giuzjcy5l1m7hd6bnqtw.'
	                                   ;待排序的原始字符串（包含字母、数字和点号）
;-------------------------------------------------------------------------------
	pgdt             dw 0		   ;GDTR数据结构：低2字节为GDT界限（由代码动态填入）
                         dd 0x00007e00     ;GDTR数据结构：高4字节为GDT的32位物理基地址
;-------------------------------------------------------------------------------
	times 510-($-$$) db 0              ;用0填充至510字节
        db 0x55,0xaa                       ;MBR签名：0x55AA

	;                        扩展知识：保护模式详解
	;=============================================================================
	;
	; 【一、为什么要从实模式切换到保护模式？】
	;   实模式（Real Mode）的局限性：
	;     - 只能访问1MB内存（20位地址线，地址范围 0x00000~0xFFFFF）
	;     - 没有内存保护机制，任何程序都可以访问任意地址，容易导致系统崩溃
	;     - 没有特权级区分，用户程序和操作系统拥有相同的权限
	;     - 段大小最大只有64KB（16位偏移量）
	;
	;   保护模式（Protected Mode）的优势：
	;     - 32位地址线，可以寻址4GB内存空间（0x00000000~0xFFFFFFFF）
	;     - 提供内存保护：每个段都有访问权限检查，越权访问会触发异常
	;     - 4个特权级（Ring 0~Ring 3）：Ring 0为内核态最高权限，Ring 3为用户态最低权限
	;     - 支持虚拟内存和分页机制
	;     - 段大小可达4GB（配合G位粒度设置）
	;
	; 【二、GDT（全局描述符表，Global Descriptor Table）】
	;   GDT是保护模式的核心数据结构，存放在内存中，由一系列段描述符组成。
	;   每个段描述符占8字节（64位），描述一个内存段的基地址、界限和属性。
	;
	;   段描述符的格式（8字节 = 64位）：
	;   高32位：
	;   |31----24|23|22|21|20|19---16|15|14-13|12|11---8|7------0|
	;   |Base    |G |D/B|L |AVL|Limit |P | DPL |S | TYPE |Base    |
	;   |31-24   |  |   |  |   |19-16 |  |     |  |      |23-16   |
	;   低32位：
	;   |31-----------16|15-----------0|
	;   |Base 15-0      |Limit 15-0    |
	;
	;   各属性位详解：
	;     G（Granularity，粒度位）：
	;       - G=0：段界限以字节为单位，段最大为 1MB（界限值 0xFFFFF × 1B）
	;       - G=1：段界限以4KB为单位，段最大为 4GB（界限值 0xFFFFF × 4KB = 4GB）
	;
	;     D/B（Default Operation Size / Default Stack Pointer Size）：
	;       - D/B=0：16位段，操作数和地址默认16位，堆栈使用SP
	;       - D/B=1：32位段，操作数和地址默认32位，堆栈使用ESP
	;
	;     L（Long Mode，64位模式标志）：
	;       - 在IA-32模式下必须置0，仅在IA-32e（64位）模式下使用
	;
	;     AVL（Available，可用位）：
	;       - 留给操作系统使用，处理器不使用该位
	;
	;     P（Present，段存在位）：
	;       - P=1：段在内存中存在
	;       - P=0：段不在内存中（已被换出到磁盘），访问时触发#NP异常
	;
	;     DPL（Descriptor Privilege Level，描述符特权级）：
	;       - 2位，取值范围0~3，0为最高特权级，3为最低
	;       - 控制对该段的访问权限：CPL <= DPL 才能访问
	;
	;     S（Descriptor Type，描述符类型位）：
	;       - S=1：代码段或数据段（非系统段）
	;       - S=0：系统段（如TSS、门描述符等）
	;
	;     TYPE（类型字段，4位）：
	;       代码段（S=1, TYPE高位=1）：X R C A
	;         - X（Execute，可执行）：代码段必须为1
	;         - R（Readable，可读）：1=可读可执行，0=仅执行
	;         - C（Conforming，一致性）：1=一致代码段（低特权级可调用）
	;         - A（Accessed，已访问）：处理器访问后自动置1
	;       数据段（S=1, TYPE高位=0）：X W E A
	;         - X=0（不可执行，数据段标志）
	;         - W（Writable，可写）：1=可读可写，0=只读
	;         - E（Expand-Down，向下扩展）：1=堆栈段（向下增长），0=普通数据段
	;         - A（Accessed，已访问）：处理器访问后自动置1
	;
	; 【三、GDTR寄存器（GDT Register）】
	;   GDTR是一个48位（6字节）的专用寄存器，用于存放GDT的位置和大小：
	;     - 低16位：GDT的界限值（= GDT总字节数 - 1）
	;     - 高32位：GDT的32位线性基地址
	;   使用 lgdt 指令加载：lgdt [内存地址]，该地址处存放6字节的GDT信息
	;
	; 【四、A20地址线（A20 Gate）】
	;   8086只有20根地址线（A0~A19），地址范围0~1MB。
	;   当地址超过1MB时会自动回卷（wraparound）到0。
	;   80286及以后的CPU有更多地址线，但为了兼容8086，A20默认被屏蔽。
	;   要访问1MB以上的地址空间，必须打开A20地址线（第21根地址线）。
	;   快速A20方法：通过端口0x92（系统控制端口A），将bit 1置1即可开启A20。
	;     in al, 0x92
	;     or al, 0000_0010B
	;     out 0x92, al
	;
	; 【五、CR0寄存器与PE位】
	;   CR0是控制寄存器0，其bit 0是PE位（Protection Enable，保护使能）：
	;     - PE=0：处理器工作在实模式
	;     - PE=1：处理器工作在保护模式
	;   切换步骤：读出CR0 -> 将bit 0置1 -> 写回CR0
	;
	; 【六、段选择子（Segment Selector）】
	;   在保护模式下，段寄存器中存放的不再是段基地址，而是16位的段选择子：
	;   |15-----------3|2 |1---0|
	;   | 索引（Index） |TI| RPL |
	;   - Index（13位）：GDT/LDT中描述符的索引号，最多可索引8192个描述符
	;   - TI（1位）：Table Indicator，0=从GDT中选择，1=从LDT中选择
	;   - RPL（2位）：请求特权级（Requested Privilege Level），0~3
	;   例如：选择子0x0008 = 0000_0000_0000_1_0_00，即GDT中索引1，TI=0，RPL=0
	;
	; 【七、流水线刷新（Pipeline Flush）】
	;   设置PE位后，处理器虽已进入保护模式，但流水线中可能还有实模式下预取的指令。
	;   必须执行一条远跳转（jmp far）来清空指令预取队列并刷新流水线，
	;   同时将保护模式下的代码段选择子加载到CS寄存器中。
	;
	; 【八、32位模式下的堆栈操作】
	;   在32位保护模式下：
	;     - push 操作使 ESP 减4（而非实模式下SP减2），然后将32位数据写入栈顶
	;     - pop 操作从栈顶读取32位数据，然后 ESP 加4
	;     - 即使 push byte 立即数，也会将其扩展为32位后压栈，ESP仍然减4
	;
	;=============================================================================

	;===== 第一步：初始化实模式下的堆栈段和栈指针 =====
	;BIOS加载MBR到0x0000:0x7C00后，CS=0x0000, IP=0x7C00
	;将SS设为与CS相同（即0x0000），SP指向0x7C00，栈向下增长
	mov ax,cs
	mov ss,ax			   ;SS=CS=0x0000，堆栈段基址与代码段相同
	mov sp,0x7c00				;SP=0x7C00，栈从0x7C00向低地址增长（0x7BFF->0x0000）

	;===== 第二步：计算GDT所在的逻辑段地址 =====
	;GDT存放在物理地址0x7E00处，需要将其转换为"段地址:偏移"的形式
	;因为实模式下DS只能存放16位段地址，所以需要做除法：物理地址 / 16 = 段地址...偏移
	mov ax,[cs:gdt_base+0x7c00]        ;取GDT物理地址的低16位（0x7E00）
	mov dx,[cs:gdt_base+0x7c00+0x02]   ;取GDT物理地址的高16位（0x0000）
	mov bx,16
	div bx            			;DX:AX / 16，商AX=段地址，余数DX=偏移
	mov ds,ax                          ;DS=段地址，使DS指向GDT所在的内存段
	mov bx,dx                          ;BX=段内偏移，作为后续写入描述符的基准地址

	;===== 第三步：在GDT中创建段描述符 =====

	;--- 创建0#描述符（空描述符，处理器强制要求GDT第一项必须为全零） ---
	;如果将0#选择子加载到段寄存器并访问内存，处理器会产生#GP（一般保护异常）
	mov dword [bx+0x00],0x00	;低32位全零——偏移地址在BX中，以BX为基准写入GDT[0]
	mov dword [bx+0x04],0x00  	;高32位全零——每个段描述符占64位（8字节）

	;--- 创建#1描述符：保护模式下的代码段描述符 ---
	;进入保护模式后，处理器从该段执行代码
	;基地址=0x00007C00（MBR加载地址），段界限=0x001FF（512字节=MBR扇区大小）
	mov dword [bx+0x08],0x7c0001ff   ;低32位：基地址低16位=0x7C00，段界限低16位=0x01FF
	mov dword [bx+0x0c],0x00409800   ;高32位：各属性位编码（见下方分析）
	;|----------------------------------------------------------------------------|
	;|                |  |  |  |   |  LIMIT |  |     |  |        |                |
	;|    BASE:31-24  |G |DB|L |AVL| 19--16 |P |DPL  |S |  TYPE  |    BASE:23-16  |
	;|----------------|--|--|--|---|--------|--|-----|--|--------|----------------|
	;|    31--24      |23|22|21|20 | 19--16 |15|14-13|12|  11-8  |      7--0      |
	;|----------------|--|--|--|---|--------|--|-----|--|--------|----------------|
	;|    00000000    |0 |1 |0 |0  |  0000  |1 |  00 |1 |  1000  |    00000000    |
	;|--------------------------------------|-------------------------------------|
	;|           0040                       |              9800                   |
	;
	;|--------------------------------------|-------------------------------------|
	;|       SEGMENT BASE 15-0              |          SEGMENT limit 15-0         |
	;|--------------------------------------|-------------------------------------|
	;|           7C00                       |              01FF                   |
	;|--------------------------------------|-------------------------------------|
	;
	;  属性解读：
	;    基地址 = 0x00007C00（分布在3个字段中：Base 31-24=0x00, Base 23-16=0x00, Base 15-0=0x7C00）
	;    段界限 = 0x001FF（Limit 19-16=0x0, Limit 15-0=0x01FF）
	;    G=0：粒度为字节，段大小 = 界限+1 = 0x200 = 512字节
	;    D/B=1：32位代码段，默认使用32位操作数和地址
	;    L=0：非64位模式
	;    AVL=0：未使用
	;    P=1：段在内存中存在
	;    DPL=00：最高特权级（Ring 0，内核级别）
	;    S=1：非系统段（代码/数据段）
	;    TYPE=1000：只可执行的代码段（X=1,R=0,C=0,A=0）

	;--- 创建#2描述符：保护模式下的数据段描述符（显存映射段）---
	;文本模式下的显示缓冲区起始于物理地址0x000B8000
	;基地址=0x000B8000，段界限=0x0FFFF（64KB，覆盖整个文本模式显存区域）
	mov dword [bx+0x10],0x8000ffff     ;低32位：基地址低16位=0x8000，段界限低16位=0xFFFF
	mov dword [bx+0x14],0x0040920b     ;高32位：各属性位编码（见下方分析）
	;|----------------------------------------------------------------------------|
	;|                |  |  |  |   |  LIMIT |  |     |  |        |                |
	;|    BASE:31-24  |G |DB|L |AVL| 19--16 |P |DPL  |S |  TYPE  |    BASE:23-16  |
	;|----------------|--|--|--|---|--------|--|-----|--|--------|----------------|
	;|    31--24      |23|22|21|20 | 19--16 |15|14-13|12|  11-8  |      7--0      |
	;|----------------|--|--|--|---|--------|--|-----|--|--------|----------------|
	;|    00000000    |0 |1 |0 |0  |  0000  |1 |  00 |1 |  0010  |    00001011    |
	;|--------------------------------------|-------------------------------------|
	;|           0040                       |              920b                   |
	;
	;|--------------------------------------|-------------------------------------|
	;|       SEGMENT BASE 15-0              |          SEGMENT limit 15-0         |
	;|--------------------------------------|-------------------------------------|
	;|           8000                       |              FFFF                   |
	;|--------------------------------------|-------------------------------------|
	;
	;  属性解读：
	;    基地址 = 0x000B8000（Base 31-24=0x00, Base 23-16=0x0B, Base 15-0=0x8000）
	;    段界限 = 0x0FFFF（64KB）
	;    G=0：字节粒度
	;    D/B=1：32位数据段
	;    P=1：段存在
	;    DPL=00：Ring 0
	;    S=1：代码/数据段
	;    TYPE=0010：可读可写的数据段（X=0,W=1,E=0,A=0）

	;--- 创建#3描述符：保护模式下的堆栈段描述符 ---
	;基地址=0x00000000，段界限=0x07A00
	;堆栈段TYPE=0110，E=1表示向下扩展（expand-down），用于堆栈
	mov dword [bx+0x18],0x00007a00     ;低32位：基地址低16位=0x0000，段界限低16位=0x7A00
	mov dword [bx+0x1c],0x00409600     ;高32位：各属性位编码（见下方分析）
	;|----------------------------------------------------------------------------|
	;|                |  |  |  |   |  LIMIT |  |     |  |        |                |
	;|    BASE:31-24  |G |DB|L |AVL| 19--16 |P |DPL  |S |  TYPE  |    BASE:23-16  |
	;|----------------|--|--|--|---|--------|--|-----|--|--------|----------------|
	;|    31--24      |23|22|21|20 | 19--16 |15|14-13|12|  11-8  |      7--0      |
	;|----------------|--|--|--|---|--------|--|-----|--|--------|----------------|
	;|    00000000    |0 |1 |0 |0  |  0000  |1 |  00 |1 |  0110  |    00000000    |
	;|--------------------------------------|-------------------------------------|
	;|           0040                       |              9600                   |
	;
	;|--------------------------------------|-------------------------------------|
	;|       SEGMENT BASE 15-0              |          SEGMENT limit 15-0         |
	;|--------------------------------------|-------------------------------------|
	;|           0000                       |              7a00                   |
	;|--------------------------------------|-------------------------------------|
	;
	;  属性解读：
	;    基地址 = 0x00000000
	;    段界限 = 0x07A00
	;    G=0：字节粒度
	;    D/B=1：32位堆栈段，push/pop操作使用ESP，每次操作4字节
	;    P=1：段存在
	;    DPL=00：Ring 0
	;    S=1：代码/数据段
	;    TYPE=0110：向下扩展、可读可写的数据段（X=0,W=1,E=1,A=0）——堆栈段

	;===== 第四步：初始化GDTR寄存器 =====
	;GDT共4个描述符（#0~#3），每个8字节，共32字节
	;GDTR中的界限值 = 总字节数 - 1 = 32 - 1 = 31
	mov word [cs:gdt_size+0x7c00],31  ;将界限值31写入GDTR数据结构的低2字节

	;将GDTR数据结构（6字节：2字节界限 + 4字节基地址）加载到GDTR寄存器
	lgdt [cs: gdt_size+0x7c00]

	;===== 第五步：打开A20地址线 =====
	;通过0x92端口（系统控制端口A，位于南桥芯片内）的bit 1控制A20
	;先读出当前值，将bit 1置1，再写回——这样不会影响其他位
	in al,0x92                         ;从端口0x92读取当前值
	or al,0000_0010B                   ;将bit 1置1（开启A20），其余位不变
	out 0x92,al                        ;写回端口0x92，A20地址线打开

	;===== 第六步：关中断 =====
	;保护模式下的中断机制（IDT/中断门）尚未建立
	;如果此时发生中断，处理器会因找不到中断处理程序而产生异常，导致系统崩溃
	cli                                ;清除IF标志位，禁止可屏蔽中断

	;===== 第七步：设置CR0的PE位，进入保护模式 =====
	;将CR0的bit 0（PE位）置1，处理器从实模式切换到保护模式
	mov eax,cr0                        ;读取CR0寄存器的当前值
	or eax,1                           ;将bit 0（PE位）置1
	mov cr0,eax                        ;写回CR0，此刻起处理器进入保护模式！

	;===== 第八步：远跳转刷新流水线，正式进入保护模式代码 =====
	;此时流水线中可能还有实模式下预取/解码的指令
	;必须用远跳转（jmp far）来清空预取队列、刷新流水线，并加载CS段选择子
	;选择子0x0008指向GDT中索引#1的代码段描述符
	jmp dword 0x0008:flush             ;16位的描述符选择子：32位偏移
	                                   ;远跳转清空流水线并串行化处理器
	;选择子0x0008的二进制分解：
	;|    描述符索引               |TI |  RPL |
	;|------------------------|---|------|
	;|       15--3            | 2 | 1--0 |
	;|------------------------|---|------|
	;|      0000 0000 0000 1  | 0 |  00  |
	;|------------------------|---|------|
	;|             0008                  |
	;  Index=1（GDT中第1个描述符），TI=0（使用GDT），RPL=00（Ring 0）

	[bits 32] 		           ;告诉汇编器以下代码按32位模式编码
flush:
	;===== 进入32位保护模式后，加载数据段选择子到DS =====
	;选择子0x0010 = 0000_0000_0001_0_000，索引号#2，TI=0（GDT），RPL=00
	;#2号描述符指向显存段（基地址0x000B8000）
	mov cx,00000000000_10_000B        ;加载数据段选择子(0x10)，索引号为2
	mov ds,cx                          ;DS现在指向显存段，偏移0x0000对应物理地址0xB8000

	;===== 在屏幕第9行显示"Protect mode OK" =====
	;文本模式下，每行160字节（80字符×2字节：ASCII码+显示属性）
	;160×8=第9行起始偏移（从第0行计算），每个字符占2字节（低字节=字符，高字节=属性）
	;此处只写字符字节，属性字节使用显存默认值
	mov byte [0x00 + 160*8],'P'
	mov byte [0x02 + 160*8],'r'
	mov byte [0x04 + 160*8],'o'
	mov byte [0x06 + 160*8],'t'
	mov byte [0x08 + 160*8],'e'
	mov byte [0x0a + 160*8],'c'
	mov byte [0x0c + 160*8],'t'
	mov byte [0x0e + 160*8],' '
	mov byte [0x10 + 160*8],'m'
	mov byte [0x12 + 160*8],'o'
	mov byte [0x14 + 160*8],'d'
	mov byte [0x16 + 160*8],'e'
	mov byte [0x18 + 160*8],' '
	mov byte [0x1a + 160*8],'O'
	mov byte [0x1c + 160*8],'K'

	;===== 第九步：验证32位保护模式下的堆栈操作 =====
	;在32位模式下，push操作会使ESP减4（不是减2），即使压入的是字节数据
	;以下代码通过实验验证这一行为
	mov cx,00000000000_11_000B         ;加载堆栈段选择子(0x18)，索引号#3
	mov ss,cx                          ;SS指向堆栈段（GDT #3描述符）
	mov esp,0x7c00                     ;初始化32位堆栈指针ESP=0x7C00

	mov ebp,esp                        ;EBP=ESP=0x7C00，保存当前栈指针用于对比
	push byte '!'                      ;压入字节'!'(0x21)——在32位模式下自动扩展为32位压栈

	sub ebp,4                          ;预期ESP应减4，所以EBP也减4以便对比
	cmp ebp,esp                        ;判断：push byte后ESP是否确实减了4？
	jnz ghalt                          ;如果不相等说明验证失败，直接停机
	pop eax                            ;弹出刚压入的值到EAX（低字节AL='!'=0x21）
	mov [0x1e + 160*8],al              ;将'!'显示在"OK"后面，完整显示"Protect mode OK!"

  ghalt:
 	hlt                                ;执行HLT停机——已禁止中断，处理器将永久停止

;-------------------------------------------------------------------------------

	gdt_size         dw 0              ;GDTR数据结构：低2字节为GDT界限（由代码动态填入）
	gdt_base         dd 0x00007e00     ;GDTR数据结构：高4字节为GDT的32位物理基地址

	times 510-($-$$) db 0              ;用0填充至510字节，使MBR总共512字节
	          db 0x55,0xaa             ;MBR签名：最后两字节必须为0x55AA，BIOS据此识别有效MBR

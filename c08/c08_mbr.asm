; 扩展知识：
;
; 一、SECTION 伪指令与 align/vstart 参数
;     SECTION name align=16 vstart=0x7c00
;     - align=16：表示该节在内存中按16字节边界对齐，这是8086段地址的基本要求。
;       段地址左移4位得到物理地址，因此段的起始物理地址必须是16的倍数。
;     - vstart=0x7c00：设置该节内标号（label）的起始计算地址。
;       BIOS将MBR加载到物理地址0x7C00处，因此节内所有标号的地址都以0x7C00为基准。
;       如果不指定vstart，标号地址将从整个汇编文件的开头开始累计计算。
;       vstart只影响标号的地址计算，不影响实际的加载位置。
;
; 二、equ 伪指令（等价伪指令）
;     app_lba_start equ 100
;     - equ 在编译期将符号名替换为指定的常量值，类似C语言的 #define。
;     - equ 不会在目标文件中分配任何存储空间，不占用汇编地址。
;     - 与 db/dw/dd 不同，equ 定义的是纯粹的编译期常量，无法被修改。
;
; 三、硬盘I/O端口（主IDE控制器 0x1F0~0x1F7）
;     x86通过独立的I/O地址空间访问硬盘控制器，使用 in/out 指令。
;     端口        读取时的含义          写入时的含义
;     0x1F0      数据寄存器（16位）    数据寄存器（16位）
;     0x1F1      错误寄存器            特征寄存器
;     0x1F2      扇区计数              扇区计数（要读写的扇区数）
;     0x1F3      LBA地址 bit[7:0]     LBA地址 bit[7:0]
;     0x1F4      LBA地址 bit[15:8]    LBA地址 bit[15:8]
;     0x1F5      LBA地址 bit[23:16]   LBA地址 bit[23:16]
;     0x1F6      LBA地址 bit[27:24]   LBA地址 bit[27:24]
;                + 驱动器号 + 模式     + 驱动器号 + 模式
;                高4位：1110=LBA主盘   高4位：1110=LBA主盘
;     0x1F7      状态寄存器            命令寄存器
;                                      0x20=读扇区命令
;                                      0x30=写扇区命令
;     状态寄存器各位含义：
;       bit7 (BSY)  = 1 表示硬盘忙，正在执行命令
;       bit6 (DRDY) = 1 表示驱动器就绪
;       bit3 (DRQ)  = 1 表示数据请求就绪，可以进行数据传输
;       bit0 (ERR)  = 1 表示上一条命令执行出错
;     读硬盘的标准流程：设置扇区数→设置LBA地址→发送读命令→等待就绪→读取数据
;
; 四、LBA（逻辑块寻址）vs CHS（柱面-磁头-扇区）寻址
;     - CHS寻址：通过柱面号(Cylinder)、磁头号(Head)、扇区号(Sector)三个参数定位扇区。
;       这种方式与磁盘的物理结构绑定，不同磁盘的CHS参数不同，编程复杂。
;     - LBA寻址：将磁盘上所有扇区从0开始线性编号（逻辑扇区号）。
;       LBA28模式使用28位地址，最大支持 2^28 × 512 = 128GB 容量。
;       硬盘控制器内部自动完成 LBA→CHS 的转换，对程序员透明。
;       LBA28的28位地址分布在端口0x1F3~0x1F6中（0x1F6仅使用低4位）。
;
; 五、段重定位（Segment Relocation）
;     - 用户程序在编译时并不知道自己会被加载到内存的哪个物理地址。
;     - 程序头部的段地址（如 section.code_1.start）记录的是相对于程序开头的偏移量。
;     - 加载器（MBR）将程序加载到指定物理地址后，需要将这些偏移量转换为实际的段地址。
;     - 转换公式：实际段地址 = (程序起始物理地址 + 段内偏移) >> 4
;     - 重定位后，用户程序中所有段寄存器才能正确指向各自的段。
;
; 六、jmp far [address]（间接远跳转）
;     - 这是一条间接远跳转指令，从内存地址处读取目标的 IP 和 CS。
;     - 内存布局：[address] 处存放偏移地址（2字节），[address+2] 处存放段地址（2字节）。
;     - 执行时：CS ← [address+2]，IP ← [address]，实现跨段跳转。
;     - 本程序中 jmp far [0x04] 从用户程序头部读取入口点的偏移和段地址，跳转到用户程序。
;===============================================================================

         app_lba_start equ 100           ;声明常数（用户程序起始逻辑扇区号）
                                         ;equ是编译期替换，不会在目标文件中占用任何存储空间
                                    
SECTION mbr align=16 vstart=0x7c00     ;MBR段，16字节对齐，标号基址从0x7C00开始

         ;===== 第一步：初始化堆栈段 =====
         ;将SS:SP设为0x0000:0x0000，由于push先减SP再写入，
         ;第一次push时SP会回绕到0xFFFE，实际栈顶在段末尾（64KB处）
         mov ax,0
         mov ss,ax
         mov sp,ax

         ;===== 第二步：计算用户程序的加载段地址 =====
         ;phy_base存放的是32位物理地址0x10000，将其除以16得到段地址0x1000
         ;使用cs:前缀是因为此时DS尚未设置，而CS已经由BIOS跳转时设为0x0000
         mov ax,[cs:phy_base]            ;取32位物理地址的低16位
         mov dx,[cs:phy_base+0x02]       ;取32位物理地址的高16位（DX:AX=0x0001:0x0000）
         mov bx,16
         div bx                          ;DX:AX ÷ 16，商AX=段地址，余数DX=0
         mov ds,ax                       ;DS指向用户程序加载段，用于读取程序头部
         mov es,ax                       ;ES也指向同一段，后续重定位时使用

         ;===== 第三步：从硬盘读取用户程序的第一个扇区（包含程序头部） =====
         xor di,di                       ;DI:SI组成32位逻辑扇区号的高16位，清零
         mov si,app_lba_start            ;SI=100，用户程序从硬盘第100个逻辑扇区开始存放
         xor bx,bx                       ;目标偏移地址BX=0，加载到DS:0x0000处
         call read_hard_disk_0           ;调用读盘子程序，读取第一个扇区（512字节）

         ;===== 第四步：根据程序头部信息计算还需要读取多少个扇区 =====
         ;程序头部前4字节（[0x00]~[0x03]）存放程序总长度（32位，低位在前）
         mov dx,[2]                      ;DX=程序总长度的高16位（注意曾误写为ds，调试20分钟）
         mov ax,[0]                      ;AX=程序总长度的低16位
         mov bx,512                      ;每个扇区512字节
         div bx                          ;DX:AX ÷ 512，商AX=完整扇区数，余数DX=不足一扇区的字节数
         cmp dx,0
         jnz @1                          ;余数不为0，说明有不完整扇区，AX已是正确的剩余扇区数
         dec ax                          ;余数为0表示刚好整除，但第一个扇区已读，所以总数减1
   @1:
         cmp ax,0                        ;如果AX=0，说明整个程序不超过512字节，无需继续读取
         jz direct

         ;===== 第五步：循环读取剩余的所有扇区 =====
         push ds                         ;保存DS（指向用户程序头部段），后面要修改DS

         mov cx,ax                       ;CX=剩余要读取的扇区数，作为loop指令的循环计数器
   @2:
         mov ax,ds
         add ax,0x20                     ;段地址+0x20，物理地址前进512字节（0x20×16=512）
         mov ds,ax                       ;DS指向下一个512字节的内存区域

         xor bx,bx                       ;偏移地址归零，每次都从段首开始写入
         inc si                          ;逻辑扇区号加1，指向下一个待读取的扇区
         call read_hard_disk_0           ;读取一个扇区到DS:BX处
         loop @2                         ;CX减1，不为0则继续循环读取

         pop ds                          ;恢复DS指向用户程序头部段，后续需要访问头部信息

         ;===== 第六步：重定位用户程序的入口点 =====
   direct:
         mov dx,[0x08]                   ;取入口点代码段的32位段内偏移的高16位
         mov ax,[0x06]                   ;取入口点代码段的32位段内偏移的低16位
         call calc_segment_base          ;将段内偏移转换为实际的段地址
         mov [0x06],ax                   ;将计算出的实际段地址回填到头部，供远跳转使用

         ;===== 第七步：遍历段重定位表，逐一修正每个段的基地址 =====
         mov cx,[0x0a]                   ;CX=重定位表项的数量（需要重定位的段数）
         mov bx,0x0c                     ;BX=重定位表在头部中的起始偏移地址

 realloc:
         mov dx,[bx+0x02]               ;取当前重定位项的高16位（32位段偏移的高字）
         mov ax,[bx]                     ;取当前重定位项的低16位（32位段偏移的低字）
         call calc_segment_base          ;将段偏移转换为加载后的实际段地址
         mov [bx],ax                     ;将实际段地址回填，覆盖原来的偏移值
         add bx,4                        ;BX指向下一个重定位项（每项占4字节：2字节低位+2字节高位）
         loop realloc                    ;CX减1，不为0则继续处理下一项

         ;===== 第八步：跳转到用户程序执行 =====
         ;间接远跳转：从内存地址[0x04]处读取2字节偏移(IP)和2字节段地址(CS)
         ;此时[0x04]和[0x06]已被重定位为正确的入口点地址
         jmp far [0x04]                  ;CS:IP ← 用户程序入口点，控制权移交给用户程序  
 
;-------------------------------------------------------------------------------
read_hard_disk_0:                        ;从硬盘读取一个逻辑扇区的子程序
                                         ;输入参数：DI:SI=32位起始逻辑扇区号（LBA地址）
                                         ;          DS:BX=读取数据的目标内存缓冲区地址
                                         ;输出：数据写入DS:BX开始的512字节内存区域
         push ax
         push bx
         push cx
         push dx

         ;--- 步骤1：设置要读取的扇区数量 ---
         mov dx,0x1f2                    ;端口0x1F2：扇区计数寄存器
         mov al,1
         out dx,al                       ;向端口写入1，表示读取1个扇区

         ;--- 步骤2：设置28位LBA地址（分4次写入4个端口） ---
         inc dx                          ;DX=0x1F3：LBA地址的bit[7:0]
         mov ax,si                       ;SI存放LBA低16位
         out dx,al                       ;写入LBA地址的最低8位（bit[7:0]）

         inc dx                          ;DX=0x1F4：LBA地址的bit[15:8]
         mov al,ah                       ;AH中是SI的高8位
         out dx,al                       ;写入LBA地址的bit[15:8]

         inc dx                          ;DX=0x1F5：LBA地址的bit[23:16]
         mov ax,di                       ;DI存放LBA高16位
         out dx,al                       ;写入LBA地址的bit[23:16]

         inc dx                          ;DX=0x1F6：LBA地址bit[27:24] + 驱动器/模式选择
         mov al,0xe0                     ;0xe0=11100000b：bit7=1, bit6=1(LBA模式), bit5=1, bit4=0(主盘)
         or al,ah                        ;将DI高8位的低4位合并为LBA地址的bit[27:24]
         out dx,al

         ;--- 步骤3：发送读命令 ---
         inc dx                          ;DX=0x1F7：命令/状态寄存器
         mov al,0x20                     ;0x20=读扇区命令（Read Sectors）
         out dx,al

         ;--- 步骤4：等待硬盘准备好数据 ---
  .waits:
         in al,dx                        ;读取0x1F7状态寄存器
         and al,0x88                     ;仅保留bit7(BSY)和bit3(DRQ)两位
         cmp al,0x08                     ;检查：BSY=0(不忙) 且 DRQ=1(数据就绪)
         jnz .waits                      ;条件不满足则继续轮询等待

         ;--- 步骤5：从数据端口读取512字节（256个字）到内存 ---
         mov cx,256                      ;一个扇区=512字节=256个字（每次读2字节）
         mov dx,0x1f0                    ;端口0x1F0：16位数据寄存器
  .readw:
         in ax,dx                        ;从数据端口读取一个字（16位）到AX
         mov [bx],ax                     ;将AX写入DS:BX指向的内存
         add bx,2                        ;内存指针前进2字节
         loop .readw                     ;CX减1，不为0则继续读取

         pop dx
         pop cx
         pop bx
         pop ax

         ret

;-------------------------------------------------------------------------------
calc_segment_base:                       ;将32位段内偏移转换为16位实模式段地址的子程序
                                         ;输入参数：DX:AX=32位段偏移（相对于程序开头的偏移量）
                                         ;输出：AX=加载后的实际16位段基地址
                                         ;原理：实际物理地址 = 程序基址 + 段偏移，再右移4位得到段地址
         push dx

         add ax,[cs:phy_base]            ;低16位相加：AX = AX + phy_base低16位
         adc dx,[cs:phy_base+0x02]       ;高16位带进位加：DX = DX + phy_base高16位 + CF
         shr ax,4                        ;AX右移4位，保留bit[15:4]到bit[11:0]
         ror dx,4                        ;DX循环右移4位，原bit[3:0]移到bit[15:12]
         and dx,0xf000                   ;只保留DX的高4位（来自原DX的低4位）
         or ax,dx                        ;合并：AX的高4位来自DX，低12位来自原AX，得到完整的16位段地址

         pop dx

         ret

;-------------------------------------------------------------------------------
         phy_base dd 0x10000             ;用户程序被加载的物理起始地址（64KB处，即0x10000）
                                         ;对应段地址为 0x10000 >> 4 = 0x1000

 times 510-($-$$) db 0                  ;用零填充剩余空间，使MBR恰好为512字节
                  db 0x55,0xaa           ;MBR有效标志（0x55AA），BIOS据此判断是否为可引导扇区
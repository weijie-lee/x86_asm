         ;===============================================================================
         ; 扩展知识（Extended Knowledge）
         ;===============================================================================
         ;
         ; 【栈段的设置：SS:SP】
         ;   SS（Stack Segment）和 SP（Stack Pointer）共同决定栈顶的物理地址。
         ;   物理地址 = SS × 16 + SP。
         ;   栈是向下增长的（从高地址向低地址方向增长）。
         ;   例如本程序设置 SS=0, SP=0，则栈顶物理地址为 0x00000。
         ;   由于 SP=0x0000，第一次 push 时 SP 先减2变为 0xFFFE，
         ;   实际写入地址为 SS:0xFFFE，即物理地址 0x0FFFE（64KB段的顶部），
         ;   这样就实现了利用整个 64KB 段作为栈空间。
         ;
         ; 【实模式下栈的 push/pop 操作】
         ;   push 操作（16位模式）：SP = SP - 2，然后将16位数据写入 SS:SP。
         ;   pop  操作（16位模式）：从 SS:SP 读取16位数据，然后 SP = SP + 2。
         ;   每次压栈/弹栈操作，SP 的变化量为 2（因为一次操作16位 = 2字节）。
         ;
         ; 【cmp 指令】
         ;   cmp 指令执行的是减法运算（目的操作数 - 源操作数），但不保存结果，
         ;   仅根据运算结果设置标志寄存器（FLAGS）中的各标志位：
         ;     ZF（零标志）：结果为零时置1
         ;     SF（符号标志）：结果为负时置1
         ;     OF（溢出标志）：有符号溢出时置1
         ;     CF（进位标志）：无符号借位时置1
         ;   后续的条件跳转指令根据这些标志位决定是否跳转。
         ;
         ; 【jle 指令（Jump if Less or Equal）】
         ;   jle 跳转条件：ZF=1 或者 SF≠OF。
         ;   即：当比较结果为"小于或等于"时跳转（有符号比较）。
         ;   ZF=1 表示两数相等；SF≠OF 表示前者小于后者（有符号意义下）。
         ;
         ; 【or dl,0x30 与 add dl,0x30 的区别】
         ;   两者都可以将数字 0~9 转换为对应的 ASCII 码 '0'~'9'。
         ;   ASCII '0' = 0x30 = 0011_0000b。
         ;   数字 0~9 的二进制为 0000_0000b ~ 0000_1001b（高4位都是0）。
         ;   or dl,0x30 ：按位或操作，将 bit5 和 bit4 置1，低4位不受影响。
         ;     因为数字 0~9 的 bit5、bit4 本来就是0，所以 or 的效果等同于加法。
         ;   add dl,0x30：算术加法，直接加上 0x30。
         ;   两者效果完全一样，但 or 是位运算，速度上在早期CPU中可能略快，
         ;   且语义上更能体现"设置高位"的意图。
         ;
         ; 【NASM 中 @ 前缀的局部标号】
         ;   在 NASM 汇编器中，以 @ 开头的标号（如 @g、@f、@d、@a）是局部标号。
         ;   局部标号的作用域限定在两个非局部标号之间，
         ;   这样可以在不同的代码段中使用相同名称的局部标号而不会冲突。
         ;   这有助于保持代码的整洁和模块化。
         ;
         ; 【高斯求和公式】
         ;   1 + 2 + 3 + ... + n = n × (n + 1) / 2
         ;   所以 1 + 2 + 3 + ... + 100 = 100 × 101 / 2 = 5050
         ;   本程序使用循环累加的方式计算，结果存放在 AX 寄存器中。
         ;   计算完成后 AX = 0x13BA（即十进制 5050）。
         ;
         ;===============================================================================

         jmp near start
	
 message db '1+2+3+...+100='       ;待显示的字符串（公式部分），共14个字符

 start:
         mov ax,0x7c0           ;设置数据段基地址为0x7c0（物理地址0x7c00是MBR加载地址）
         mov ds,ax              ;DS=0x7c0，后续用 [si] 访问数据时基于此段

         mov ax,0xb800          ;0xb8000是文本模式显示缓冲区的起始物理地址
         mov es,ax              ;ES=0xb800，后续用 [es:di] 直接写显存

         ;===== 以下显示字符串 "1+2+3+...+100=" =====
         mov si,message         ;SI指向字符串首地址（段内偏移）
         mov di,160*8           ;DI指向显存第9行开头（每行80字符×2字节=160字节，跳过前8行BIOS信息）
         mov cx,start-message   ;CX=字符串长度（利用标号差值计算，start-message=14）
     @g:
         mov al,[si]            ;从DS:SI处取一个字符到AL
         mov [es:di],al         ;将字符写入显存的字符位置（偶数地址存ASCII码）
         inc di                 ;DI+1，指向该字符的属性位置（奇数地址存显示属性）
         mov byte [es:di],0x07  ;设置显示属性：0x07 = 黑底白字（低4位=前景色白，高4位=背景色黑）

         inc di                 ;DI+1，指向下一个字符的ASCII位置
         inc si                 ;SI+1，指向源字符串的下一个字符
         loop @g                ;CX-1，若CX≠0则继续循环

         ;===== 以下计算1到100的累加和（结果存入AX） =====
         xor ax,ax              ;AX清零，用作累加器（xor自身是清零的常用技巧）
         mov cx,1               ;CX=1，作为循环计数器，从1开始累加
     @f:
         add ax,cx              ;AX = AX + CX（累加当前值）
         inc cx                 ;CX = CX + 1（递增计数器）
         cmp cx,100             ;比较CX与100（实际执行CX-100，仅设置标志位，不保存结果）
         jle @f                 ;若CX≤100则跳转继续循环（有符号比较：ZF=1或SF≠OF）
                                ;当CX=101时不再跳转，此时AX=5050（0x13BA）

         ;===== 以下将累加和拆分为各个十进制数位，压入栈中 =====
         xor cx,cx              ;CX清零，后续用作数位计数器（记录压栈了多少个数位）
         mov ss,cx              ;SS=0，设置栈段基地址为0
         mov sp,cx              ;SP=0，栈顶指针。SP=0时第一次push会使SP变为0xFFFE，
                                ;  即使用0x00000~0x0FFFF这64KB段的顶部作为栈空间

         mov bx,10              ;BX=10，作为除数（用于逐位提取十进制数位）
     @d:
         inc cx                 ;CX+1，记录提取的数位个数（后续pop循环用作loop计数）
         xor dx,dx              ;DX清零（div指令要求DX:AX为被除数，16位除法时DX必须为0）
         div bx                 ;无符号除法：DX:AX ÷ BX → 商存AX，余数存DX
                                ;  例如第一次：5050÷10，商=505存AX，余数=0存DL
         or dl,0x30             ;将数字0~9转换为ASCII码'0'~'9'（等效于add dl,0x30）
                                ;  原理：0~9的bit5和bit4为0，or 0x30将这两位置1即得ASCII码
         push dx                ;将转换后的ASCII码压栈（低位数字先入栈，后续pop时高位先出）
         cmp ax,0               ;检查商是否为0（判断是否还有更高位需要提取）
         jne @d                 ;商不为0则继续循环提取下一位数字

         ;===== 以下从栈中弹出各数位并显示（利用栈的LIFO特性，高位先弹出，实现正序显示） =====
     @a:
         pop dx                 ;从栈中弹出一个数位的ASCII码到DX（高位数字先弹出）
         mov [es:di],dl         ;将ASCII字符写入显存（紧接在"1+2+3+...+100="之后显示）
         inc di                 ;DI+1，指向属性位置
         mov byte [es:di],0x07  ;设置显示属性：0x07 = 黑底白字
         inc di                 ;DI+1，指向下一个字符位置
         loop @a                ;CX-1，若CX≠0则继续弹出并显示下一个数位

         jmp near $             ;无限循环（$代表当前指令地址），程序在此处停住


times 510-($-$$) db 0          ;用0填充剩余空间，使整个程序恰好占满510字节
                 db 0x55,0xaa   ;MBR有效标志（最后两字节必须是0x55AA，BIOS据此判断有效引导扇区）
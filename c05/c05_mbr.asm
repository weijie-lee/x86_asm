         ;===============================================================================
         ; 扩展知识：
         ;
         ; 1. 什么是MBR（主引导记录）？
         ;    MBR位于硬盘的第一个扇区（0面0道1扇区），大小固定为512字节。
         ;    BIOS在完成硬件自检后，会将MBR的512字节加载到内存地址0x7C00处并跳转执行。
         ;    MBR的最后两个字节必须是0x55和0xAA，这是有效引导扇区的标志（魔数签名）。
         ;    如果BIOS检测不到这个签名，就不会认为该扇区是可引导的。
         ;
         ; 2. BIOS启动流程：
         ;    上电 -> POST（加电自检，Power-On Self-Test，检测CPU、内存、外设等硬件）
         ;    -> 根据启动顺序读取第一个可引导设备的MBR（第一个扇区，512字节）
         ;    -> 将MBR加载到物理内存地址0x0000:0x7C00（即线性地址0x7C00）处
         ;    -> 跳转到0x7C00开始执行MBR中的代码
         ;
         ; 3. VGA文本模式显存布局：
         ;    文本模式下，显存起始物理地址为0xB8000（段地址0xB800，偏移0x0000）。
         ;    屏幕为80列×25行，共2000个字符位置。
         ;    每个字符占2个字节：
         ;      第1个字节 = ASCII字符编码
         ;      第2个字节 = 属性字节（显示颜色）
         ;    属性字节格式：高4位[7:4]为背景色，低4位[3:0]为前景色。
         ;      Bit 7    : 闪烁位（1=闪烁）
         ;      Bit 6-4  : 背景色（0=黑,1=蓝,2=绿,3=青,4=红,5=紫,6=棕,7=白）
         ;      Bit 3    : 亮度位（1=高亮）
         ;      Bit 2-0  : 前景色（同背景色编码）
         ;    例如：0x07 = 黑底白字（背景0000=黑，前景0111=白）
         ;          0x04 = 黑底红字，0x02 = 黑底绿字
         ;    每行占用 80×2 = 160字节，第N行起始偏移 = N × 160
         ;
         ; 4. 实模式下的段:偏移寻址方式：
         ;    物理地址 = 段地址 × 16 + 偏移地址
         ;    例如：段地址0xB800，偏移0x0000 -> 物理地址 = 0xB800×16+0 = 0xB8000
         ;    再如：段地址0xB800+80=0xB850，偏移0x0000 -> 物理地址 = 0xB8500
         ;    这意味着0xB850:0x0000指向屏幕第二行的起始位置（偏移160=0xA0字节）
         ;
         ; 5. DIV指令（无符号除法）在16位模式下的行为：
         ;    当操作数为16位寄存器/内存时（如 div bx）：
         ;      被除数 = DX:AX（DX为高16位，AX为低16位，组成32位被除数）
         ;      除数   = 16位操作数（如BX）
         ;      商     -> AX
         ;      余数   -> DX
         ;    例如：DX=0, AX=153, BX=10 -> div bx -> AX=15（商）, DX=3（余数）
         ;    通过反复对10取余，可以逐位提取十进制数的每一位数字。
         ;===============================================================================

	mov ax,0xb800 + 80                 ;将段地址设为0xB850（0xB800+80=0xB850）
                                           ;物理地址=0xB850×16=0xB8500，即显存第二行起始处
                                           ;（第一行起始0xB8000，每行160字节，第二行=0xB8000+0xA0=0xB80A0）
                                           ;注意：此处+80是段地址加80，实际偏移=80×16=1280字节=8行
         mov es,ax                          ;将es段寄存器指向显存对应位置，后续用es:偏移来写屏

         ;以下显示字符串"Label offset:"
         ;每个字符占2个字节：偶数偏移放ASCII字符，奇数偏移放属性字节
         ;0x07表示黑底白字（背景=0000黑色，前景=0111白色），是最常用的显示属性
         mov byte [es:0x00],'L'            ;在屏幕对应行的第1个字符位置显示'L'
	mov byte [es:0x01],0x07            ;设置'L'的显示属性：黑底白字
         mov byte [es:0x02],'a'            ;第2个字符位置显示'a'
         mov byte [es:0x03],0x07           ;属性：黑底白字
         mov byte [es:0x04],'b'
         mov byte [es:0x05],0x07
         mov byte [es:0x06],'e'
         mov byte [es:0x07],0x07
         mov byte [es:0x08],'l'
         mov byte [es:0x09],0x07
         mov byte [es:0x0a],' '            ;显示空格字符，作为单词分隔
         mov byte [es:0x0b],0x07
         mov byte [es:0x0c],"o"
         mov byte [es:0x0d],0x07
         mov byte [es:0x0e],'f'
         mov byte [es:0x0f],0x07
         mov byte [es:0x10],'f'
         mov byte [es:0x11],0x07
         mov byte [es:0x12],'s'
         mov byte [es:0x13],0x07
         mov byte [es:0x14],'e'
         mov byte [es:0x15],0x07
         mov byte [es:0x16],'t'
         mov byte [es:0x17],0x07
         mov byte [es:0x18],':'            ;最后一个字符':'，整体显示"Label offset:"
         mov byte [es:0x19],0x07

	;mov ax,number                 ;取得标号number的汇编地址（偏移地址）
	mov ax,0x99                    ;这里直接用立即数0x99（十进制153）代替标号偏移地址进行演示
         mov bx,10                      ;除数设为10，用于逐位提取十进制数字（反复除以10取余数）

         ;设置数据段的基地址
         ;因为MBR被加载到0x0000:0x7C00，所以CS=0x0000
         ;将DS也设为0x0000，这样通过DS段访问内存时基地址为0
         mov cx,cs                      ;cx = cs = 0x0000（MBR加载时CS被BIOS设为0）
         mov ds,cx                      ;ds = 0x0000，数据段基地址为0

         ;以下通过反复除以10，提取ax中数值的每一位十进制数字
         ;div bx: DX:AX ÷ BX -> 商存入AX，余数存入DX
         ;每次的余数（DX的低8位DL）就是当前最低位的数字

         ;求个位上的数字
         mov dx,0                       ;清零DX，使被除数为DX:AX = 0:0x99 = 153
         div bx                         ;153÷10 -> AX=15（商），DX=3（余数，即个位数字）
         mov [0x7c00+number+0x00],dl    ;将个位数字3保存到number缓冲区第0个字节
                                        ;注意：必须加0x7C00前缀，因为汇编地址从0开始，
                                        ;但代码实际被加载到0x7C00处，需要手动修正地址

         ;求十位上的数字
         xor dx,dx                      ;清零DX（异或自身归零，比mov dx,0更高效，只需2字节）
         div bx                         ;15÷10 -> AX=1（商），DX=5（余数，即十位数字）
         mov [0x7c00+number+0x01],dl    ;保存十位上的数字5

         ;求百位上的数字
         xor dx,dx                      ;清零DX，准备下一次除法
         div bx                         ;1÷10 -> AX=0（商），DX=1（余数，即百位数字）
         mov [0x7c00+number+0x02],dl    ;保存百位上的数字1

         ;求千位上的数字
         xor dx,dx
         div bx                         ;0÷10 -> AX=0, DX=0（千位为0）
         mov [0x7c00+number+0x03],dl    ;保存千位上的数字0

         ;求万位上的数字
         xor dx,dx
         div bx                         ;0÷10 -> AX=0, DX=0（万位为0）
         mov [0x7c00+number+0x04],dl    ;保存万位上的数字0

         ;以下用十进制显示标号的偏移地址（从高位到低位依次显示）
         ;将数字加上0x30（字符'0'的ASCII码），即可将数字0-9转换为对应的ASCII字符'0'-'9'
         ;例如：数字5 + 0x30 = 0x35 = ASCII字符'5'

         ;显示万位数字
         mov al,[0x7c00+number+0x04]       ;从缓冲区取出万位数字
	add al,0x30                        ;数字转ASCII：加上'0'的编码0x30
	mov [es:0x1a],al                   ;写入显存偏移0x1A处（紧接"Label offset:"之后显示）
	mov byte [es:0x1b],0x04            ;属性0x04=黑底红字（背景0000=黑，前景0100=红）

         ;显示千位数字
         mov al,[0x7c00+number+0x03]
         add al,0x30
         mov [es:0x1c],al
	mov byte [es:0x1d],0x05            ;属性0x05=黑底紫色字

         ;显示百位数字
         mov al,[0x7c00+number+0x02]
         add al,0x30
         mov [es:0x1e],al
	mov byte [es:0x1f],0x06            ;属性0x06=黑底棕色字

         ;显示十位数字
         mov al,[0x7c00+number+0x01]
         add al,0x30
         mov [es:0x20],al
	mov byte [es:0x21],0x07            ;属性0x07=黑底白字

         ;显示个位数字
         mov al,[0x7c00+number+0x00]
         add al,0x30
         mov [es:0x22],al
	mov byte [es:0x23],0x05            ;属性0x05=黑底紫色字

         ;在数字末尾显示'D'，表示这是十进制（Decimal）数
         mov byte [es:0x24],'D'
	mov byte [es:0x25],0x02            ;属性0x02=黑底绿色字
          
   infi: jmp near infi                 ;无限循环，程序在此处死循环
                                       ;MBR执行完毕后没有操作系统可以返回，必须用死循环保持CPU运行
                                       ;near跳转表示段内跳转（不改变CS，只改变IP）

  number db 0,0,0,0,0                  ;定义5个字节的缓冲区，用于存放分离出的5位十进制数字
                                       ;依次存放：个位、十位、百位、千位、万位

;以下用0填充剩余空间，并在最后两个字节写入MBR签名0x55AA
;整个MBR必须恰好512字节（times指令填充0使总大小达到510字节，加上2字节签名=512字节）
  times 203 db 0                       ;填充203个0x00字节，凑满510字节
            db 0x55,0xaa               ;MBR有效签名标志，BIOS据此判断该扇区是否为有效引导扇区
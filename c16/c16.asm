         ;文件名：c16.asm
         ;文件说明：用户程序（运行于分页环境下）
         ;创建日期：2012-05-25 13:53
         ;
         ;===========================================================================
         ;  扩展知识：分页环境下的用户程序
         ;===========================================================================
         ;
         ;  【一、用户程序看到的是虚拟地址空间】
         ;    在分页环境下，用户程序运行在独立的虚拟地址空间中：
         ;      ● 程序代码从虚拟地址 0x00000000 开始加载
         ;      ● 程序中所有的内存访问（代码、数据、堆栈）都使用虚拟地址
         ;      ● 用户程序完全不知道、也不需要知道自己的物理地址
         ;      ● MMU（内存管理单元）通过页表将虚拟地址自动转换为物理地址
         ;    这意味着：
         ;      ① 多个用户程序可以使用相同的虚拟地址（如都从0开始），
         ;         但映射到不同的物理页 → 地址空间互不干扰
         ;      ② 用户程序的代码无需考虑加载到物理内存的哪个位置
         ;      ③ 内核通过页表的U/S位控制用户程序不能访问内核空间(高2GB)
         ;
         ;  【二、reserved times 256*500 db 0 的作用】
         ;    这段保留区域定义了 256×500=128,000 字节（约125KB）的空白数据。
         ;    它的目的是故意使用户程序体积变大，以演示分页加载的过程：
         ;      ● 内核的 load_relocate_program 会计算程序总大小并按4KB对齐
         ;      ● 每个4KB页需要调用 alloc_inst_a_page 分配物理页并建立映射
         ;      ● 125KB ≈ 32个4KB页，需要分配32个物理页和可能的额外页表
         ;    如果没有这段保留区，程序太小（仅占1~2个页），无法充分展示分页加载。
         ;
         ;  【三、用户程序如何调用内核服务】
         ;    用户程序通过 SALT（符号地址检索表）中的符号名引用内核服务。
         ;    内核在加载时将符号名替换为调用门选择子（SALT重定位）。
         ;    用户程序执行 call far [PrintString] 时，实际是通过调用门
         ;    从Ring3(用户)切换到Ring0(内核)执行服务例程，完成后自动返回Ring3。
         ;
         ;===========================================================================

         ;---------------------------------------------------------------------------
         ;  用户程序头部：供内核加载器解析
         ;---------------------------------------------------------------------------
         program_length   dd program_end          ;#0x00：程序总长度（字节）
         entry_point      dd start                ;#0x04：程序入口点偏移地址
         salt_position    dd salt_begin           ;#0x08：U-SALT表在程序中的起始偏移
         salt_items       dd (salt_end-salt_begin)/256 ;#0x0C：U-SALT条目数

;-------------------------------------------------------------------------------
         ;  用户SALT（U-SALT）：用户程序引用的内核服务符号表
         ;  每条目256字节，由符号名填充。内核加载时会将符号名替换为调用门选择子。
         ;-------------------------------------------------------------------------------
         salt_begin:

         PrintString      db  '@PrintString'
                     times 256-($-PrintString) db 0       ;填充至256字节

         TerminateProgram db  '@TerminateProgram'
                     times 256-($-TerminateProgram) db 0
;-------------------------------------------------------------------------------

         ;--- 保留区：故意创建约125KB的空白数据以演示多页加载机制 ---
         ;  256*500 = 128000 字节 ≈ 32个4KB页
         ;  内核加载此程序时需为这些数据分配物理页并建立页表映射
         reserved  times 256*500 db 0            ;保留空白区，演示分页加载

;-------------------------------------------------------------------------------
         ReadDiskData     db  '@ReadDiskData'
                     times 256-($-ReadDiskData) db 0

         PrintDwordAsHex  db  '@PrintDwordAsHexString'
                     times 256-($-PrintDwordAsHex) db 0

         salt_end:                                ;U-SALT表结束标记

         message_0        db  0x0d,0x0a,
                          db  '  ............User task is running with '
                          db  'paging enabled!............',0x0d,0x0a,0
                                                  ;用户任务启动提示信息

         space            db  0x20,0x20,0         ;两个空格，用于格式化输出
         
;-------------------------------------------------------------------------------
      [bits 32]                                   ;32位保护模式代码
;-------------------------------------------------------------------------------

;===============================================================================
;  用户程序入口点
;  此时程序运行在Ring3（用户特权级），拥有独立的虚拟地址空间
;  所有段基地址=0、界限=4GB（平坦模型），但通过分页实现隔离
;===============================================================================
start:

         mov ebx,message_0
         call far [PrintString]             ;通过调用门远调用内核的字符串显示服务
                                            ;（Ring3→Ring0特权级切换，自动完成）

         ;--- 以十六进制显示虚拟地址空间起始处的内存内容 ---
         ;  从虚拟地址0x00000000开始，每次读取一个双字(4字节)，共显示88个
         ;  这些内容实际就是本程序头部和SALT的数据（因为程序从虚拟地址0加载）
         xor esi,esi                        ;ESI=0，从虚拟地址0开始
         mov ecx,88                         ;循环88次，显示88个双字=352字节
  .b1:
         mov ebx,space
         call far [PrintString]             ;先输出两个空格作为分隔

         mov edx,[esi*4]                    ;读取虚拟地址 ESI*4 处的双字（自动经MMU转换为物理地址）
         call far [PrintDwordAsHex]         ;以十六进制形式显示该双字

         inc esi                            ;下一个双字
         loop .b1                           ;循环直到显示完88个

         call far [TerminateProgram]        ;调用内核的任务终止服务，退出并将控制权返回内核

;-------------------------------------------------------------------------------
program_end:                                ;程序结束标记（program_length引用此标号计算总长度）
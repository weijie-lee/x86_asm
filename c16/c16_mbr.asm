         ;代码清单16-1（MBR部分）
         ;文件名：c16_mbr.asm
         ;文件说明：硬盘主引导扇区代码（含分页机制初始化准备）
         ;创建日期：2011-10-28 22:35
         ;
         ;===========================================================================
         ;  扩展知识：IA-32 分页机制（Paging Mechanism）
         ;===========================================================================
         ;
         ;  【一、二级分页概述】
         ;  IA-32 处理器在保护模式下可开启分页，将线性地址转换为物理地址。
         ;  32位线性地址被拆分为三段：
         ;
         ;    31        22 21       12 11         0
         ;   ┌───────────┬───────────┬─────────────┐
         ;   │ 目录索引   │ 页表索引   │  页内偏移    │
         ;   │ (10 bits)  │ (10 bits)  │  (12 bits)  │
         ;   └───────────┴───────────┴─────────────┘
         ;
         ;  转换流程（两级页表）：
         ;    ① CR3 寄存器 → 页目录（Page Directory, PD）的物理基地址
         ;    ② 用「目录索引」在 PD 中找到页目录项（PDE）→ 得到页表物理基地址
         ;    ③ 用「页表索引」在页表（Page Table, PT）中找到页表项（PTE）→ 得到物理页基地址
         ;    ④ 物理页基地址 + 页内偏移 = 最终物理地址
         ;
         ;  【二、页目录项（PDE）与页表项（PTE）格式】
         ;    31          12 11  9  8  7  6  5  4  3  2  1  0
         ;   ┌─────────────┬─────┬──┬──┬──┬──┬──┬──┬────┬────┐
         ;   │ 物理页框地址  │AVL  │G │PS│D │A │PCD│PWT│U/S │R/W│ P │
         ;   └─────────────┴─────┴──┴──┴──┴──┴──┴──┴────┴────┘
         ;    P   = 存在位（1=页在内存中）
         ;    R/W = 读写位（1=可读写，0=只读）
         ;    U/S = 用户/超级（1=所有特权级可访问，0=仅Ring0~2）
         ;    PWT/PCD = 缓存策略位
         ;    A   = 已访问位，D = 脏位（仅PTE有意义）
         ;    PS  = 页大小（PDE中：0=4KB页，1=4MB页）
         ;
         ;  【三、CR3 寄存器（PDBR）】
         ;    存放页目录的物理基地址（必须4KB对齐，低12位中包含PCD/PWT标志）。
         ;    每个任务可以有自己的 CR3，实现独立地址空间。
         ;
         ;  【四、CR0.PG 位（第31位）】
         ;    将 CR0 的最高位置 1 即开启分页。注意：开启分页前必须已处于保护模式。
         ;
         ;  【五、页目录自映射技巧（Self-Mapping Trick）】
         ;    在页目录的第 1023 项（最后一项）写入页目录自身的物理地址：
         ;      PD[1023] = PD物理地址 | 属性
         ;    这样做的效果：
         ;      ● 线性地址 0xFFFFF000 映射到页目录自身
         ;        （目录索引=1023 → PD，页表索引=1023 → 又是PD，即PD被当作页表使用）
         ;      ● 线性地址 0xFFC00000 + dir_index * 4096 可访问任意页表
         ;        （目录索引=1023 → PD被当作页表，页表索引=dir_index → 对应的页表）
         ;    这使得操作系统无需知道页表的物理地址，即可通过线性地址直接修改页表内容。
         ;
         ;  【六、内核高半区映射（High-Half Kernel）】
         ;    将内核映射到线性地址 0x80000000（2GB）以上：
         ;      ● 低 2GB（0x00000000 ~ 0x7FFFFFFF）：用户空间，每个任务独立
         ;      ● 高 2GB（0x80000000 ~ 0xFFFFFFFF）：内核空间，所有任务共享
         ;    这种布局的好处：
         ;      ① 内核代码/数据在任务切换时无需重新映射
         ;      ② 用户程序无法直接访问内核地址（U/S位控制）
         ;      ③ 简化系统调用时的地址空间切换
         ;
         ;===========================================================================

         core_base_address equ 0x00040000   ;常数，内核加载的起始内存地址（物理地址0x40000）
         core_start_sector equ 0x00000001   ;常数，内核的起始逻辑扇区号（LBA=1）

         ;---------------------------------------------------------------------------
         ;  MBR 入口：BIOS 将本扇区加载到 0x0000:0x7C00 后跳转至此
         ;---------------------------------------------------------------------------
         mov ax,cs                          ;CS=0x0000（BIOS跳转时设定）
         mov ss,ax                          ;设置堆栈段 SS=0
         mov sp,0x7c00                      ;栈顶指向 0x7C00（向下增长，不会覆盖MBR代码）
      
         ;计算GDT所在的逻辑段地址（实模式下需要段:偏移寻址）
         mov eax,[cs:pgdt+0x7c00+0x02]      ;从pgdt结构中取出GDT的32位物理地址
         xor edx,edx
         mov ebx,16
         div ebx                            ;物理地址÷16 → 商=段地址(eax)，余数=偏移(edx)

         mov ds,eax                         ;令DS指向GDT所在的实模式段
         mov ebx,edx                        ;EBX=GDT在该段内的起始偏移地址

         ;跳过0#号描述符的槽位（处理器要求第0个描述符必须为全零的空描述符）
         ;创建1#描述符：数据段，覆盖整个0~4GB线性地址空间（"平坦模型"）
         mov dword [ebx+0x08],0x0000ffff    ;基地址=0，段界限=0xFFFFF（配合4KB粒度=4GB）
         mov dword [ebx+0x0c],0x00cf9200    ;G=1(4KB粒度),DB=1(32位),可读写数据段,DPL=0

         ;创建2#描述符：保护模式下的初始代码段（用于MBR自身代码执行）
         mov dword [ebx+0x10],0x7c0001ff    ;基地址=0x00007C00，界限=0x1FF（512字节=1扇区）
         mov dword [ebx+0x14],0x00409800    ;G=0(字节粒度),DB=1(32位),只执行代码段,DPL=0 
        ;|----------------------------------------------------------------------------|
	;|                |  |  |  |   |  LIMIT |  |     |  |        |                |
	;|    BASE:31-24  |G |DB|L |AVL| 19--16 |P |DPL  |S |  TYPE  |    BASE:23-16  |
	;|----------------|--|--|--|---|--------|--|-----|--|--------|----------------|
	;|    31--24      |23|22|21|20 | 19--16 |15|14-13|12|  11-8  |      7--0      |
	;|----------------|--|--|--|---|--------|--|-----|--|--------|----------------|
	;|    00000000    |0 |1 |0 |0  |  0000  |1 |  00 |1 |  1000  |    00000000    |
	;|--------------------------------------|-------------------------------------|
	;|           0040                       |              9800                   |
	
	;|--------------------------------------|-------------------------------------|
	;|       SEGMENT BASE 15-0              |          SEGMENT limit 15-0         |
	;|--------------------------------------|-------------------------------------|
	;|           7C00                       |              01FF(512)              |
	;|--------------------------------------|-------------------------------------|

         ;建立3#描述符：保护模式下的堆栈段       ;基地址=0x00007C00，界限=0xFFFFE
         mov dword [ebx+0x18],0x7c00fffe    ;G=1(4KB粒度),向上扩展堆栈段
         mov dword [ebx+0x1c],0x00cf9600    ;可读写、向下扩展的数据段（堆栈段）,DPL=0

         ;建立4#描述符：显示缓冲区段（显存映射）
         mov dword [ebx+0x20],0x80007fff    ;基地址=0x000B8000，界限=0x07FFF（32KB显存）
         mov dword [ebx+0x24],0x0040920b    ;G=0(字节粒度),可读写数据段,DPL=0

         ;初始化GDTR：共5个描述符(0#~4#)，每个8字节，共40字节，界限=40-1=39
         mov word [cs:pgdt+0x7c00],39      ;描述符表的界限（字节数-1）

 	 ;将GDT信息加载到GDTR寄存器，使处理器知道GDT的位置和大小
         lgdt [cs:pgdt+0x7c00]

         ;---------------------------------------------------------------------------
         ;  打开A20地址线：使处理器能访问1MB以上的内存
         ;---------------------------------------------------------------------------
         in al,0x92                         ;读南桥芯片的系统控制端口A
         or al,0000_0010B                   ;将第1位置1（Fast A20 Gate）
         out 0x92,al                        ;写回，打开A20地址线

         cli                                ;关中断：实模式IDT即将失效，新的中断机制尚未建立

         ;---------------------------------------------------------------------------
         ;  切换到保护模式：置CR0的PE位（第0位）
         ;---------------------------------------------------------------------------
         mov eax,cr0
         or eax,1                           ;PE=1
         mov cr0,eax                        ;写回CR0，处理器进入保护模式

         ;以下进入保护模式... ...
         jmp dword 0x0010:flush             ;16位的描述符选择子(2#代码段)：32位偏移
                                            ;远跳转刷新流水线并串行化处理器，清除实模式指令预取
         [bits 32]                          ;以下为32位保护模式代码
  flush:
  	;|       index            |TI |  DPL |
	;|------------------------|---|------|
	;|       15--3            | 2 | 1--0 |
	;|------------------------|---|------|
	;|      0000 0000 0000 1  | 0 |  00  |
	;|------------------------|---|------|
	;|             0008                  |
         mov eax,0x0008                     ;加载1#数据段(0~4GB平坦模型)选择子到DS
         mov ds,eax                         ;此后DS可寻址全部4GB物理内存

         mov eax,0x0018                     ;加载3#堆栈段选择子到SS
         mov ss,eax
         xor esp,esp                        ;堆栈指针清零（栈从段基址0x7C00向下增长）

         ;===========================================================================
         ;  加载内核程序到内存（从硬盘LBA扇区读取到core_base_address）
         ;===========================================================================
         mov edi,core_base_address          ;EDI=内核的目标加载地址(0x00040000)

         mov eax,core_start_sector          ;EAX=内核起始逻辑扇区号(LBA=1)
         mov ebx,edi                        ;EBX=目标缓冲区起始地址
         call read_hard_disk_0              ;先读第一个扇区（含内核头部信息）

         ;以下根据内核头部记录的总长度，计算还需读取多少个扇区
         mov eax,[edi]                      ;[edi+0x00]=内核程序总字节数
         xor edx,edx
         mov ecx,512                        ;每扇区512字节
         div ecx                            ;EAX=总扇区数(商), EDX=余数

         or edx,edx                         ;是否有余数？
         jnz @1                             ;有余数→实际需要的扇区数=商+1，不减
         dec eax                            ;无余数→已读1个扇区，总数减1
   @1:
         or eax,eax                         ;还需要读0个扇区？（程序≤512字节的情况）
         jz setup                           ;是，直接跳到安装阶段

         ;读取内核剩余的扇区
         mov ecx,eax                        ;ECX=剩余扇区数（32位LOOP使用ECX）
         mov eax,core_start_sector
         inc eax                            ;从第2个扇区开始继续读
   @2:
         call read_hard_disk_0              ;每次读1个扇区，EBX自动后移512字节
         inc eax                            ;下一个逻辑扇区号
         loop @2                            ;循环直到读完整个内核 

 setup:
         ;===========================================================================
         ;  根据内核头部信息，在GDT中创建内核所需的段描述符
         ;===========================================================================
         mov esi,[0x7c00+pgdt+0x02]         ;通过4GB平坦段访问pgdt，取得GDT物理基地址

         ;--- 重建5#描述符：内核公用例程代码段 ---
         mov eax,[edi+0x04]                 ;内核头部偏移0x04：公用例程段的汇编起始地址
         mov ebx,[edi+0x08]                 ;内核头部偏移0x08：核心数据段的汇编起始地址
         sub ebx,eax			    ;两段之差 = 公用例程段的总长度
         dec ebx                            ;段界限 = 段长度 - 1
         add eax,edi                        ;段基地址 = 内核加载地址(0x40000) + 段内偏移
         mov ecx,0x00409800                 ;属性：G=0(字节粒度),DB=1(32位),只执行代码段,DPL=0
         call make_gdt_descriptor
         mov [esi+0x28],eax		    ;写入GDT第5项（偏移0x28=5*8）的低32位
         mov [esi+0x2c],edx		    ;写入GDT第5项的高32位

         ;--- 建立6#描述符：核心数据段 ---
         mov eax,[edi+0x08]                 ;核心数据段起始汇编地址
         mov ebx,[edi+0x0c]                 ;核心代码段起始汇编地址
         sub ebx,eax
         dec ebx                            ;核心数据段界限 = 数据段长度 - 1
         add eax,edi                        ;核心数据段基地址 = 加载地址 + 偏移
         mov ecx,0x00409200                 ;属性：G=0(字节粒度),DB=1(32位),可读写数据段,DPL=0
         call make_gdt_descriptor
         mov [esi+0x30],eax                 ;写入GDT第6项（偏移0x30=6*8）
         mov [esi+0x34],edx

         ;--- 建立7#描述符：核心代码段 ---
         mov eax,[edi+0x0c]                 ;核心代码段起始汇编地址
         mov ebx,[edi+0x00]                 ;程序总长度
         sub ebx,eax
         dec ebx                            ;核心代码段界限
         add eax,edi                        ;核心代码段基地址
         mov ecx,0x00409800                 ;属性：G=0(字节粒度),DB=1(32位),只执行代码段,DPL=0
         call make_gdt_descriptor
         mov [esi+0x38],eax                 ;写入GDT第7项（偏移0x38=7*8）
         mov [esi+0x3c],edx

         ;更新GDT界限：8个描述符(0#~7#)，共64字节，界限=64-1=63
         mov word [0x7c00+pgdt],63          ;描述符表的界限

         lgdt [0x7c00+pgdt]                 ;重新加载GDTR，使新增描述符生效

         jmp far [edi+0x10]                 ;远跳转到内核入口：偏移在[edi+0x10]，选择子在[edi+0x14]  
       
;===============================================================================
;  子过程：read_hard_disk_0 —— 通过端口I/O读取硬盘的一个逻辑扇区
;  输入：EAX=逻辑扇区号（LBA模式）
;        DS:EBX=目标缓冲区地址
;  返回：EBX=EBX+512（指向下一个可用缓冲区位置）
;===============================================================================
read_hard_disk_0:
         push eax
         push ecx
         push edx

         push eax

         mov dx,0x1f2
         mov al,1
         out dx,al                       ;向0x1F2端口写入要读取的扇区数=1

         inc dx                          ;0x1F3：LBA地址的第7~0位
         pop eax
         out dx,al                       ;写入LBA低8位

         inc dx                          ;0x1F4：LBA地址的第15~8位
         mov cl,8
         shr eax,cl
         out dx,al                       ;写入LBA的8~15位

         inc dx                          ;0x1F5：LBA地址的第23~16位
         shr eax,cl
         out dx,al                       ;写入LBA的16~23位

         inc dx                          ;0x1F6：LBA地址的第27~24位 + 驱动器号
         shr eax,cl
         or al,0xe0                      ;高4位=1110：LBA模式，主硬盘
         out dx,al

         inc dx                          ;0x1F7：命令端口
         mov al,0x20                     ;0x20=READ SECTORS命令
         out dx,al

  .waits:
         in al,dx                        ;读状态寄存器（0x1F7兼作状态端口）
         and al,0x88                     ;保留BSY(bit7)和DRQ(bit3)
         cmp al,0x08                     ;BSY=0且DRQ=1：硬盘不忙且数据已准备好
         jnz .waits                      ;否则继续等待

         mov ecx,256                     ;每扇区512字节=256个字(word)
         mov dx,0x1f0                    ;0x1F0：数据端口
  .readw:
         in ax,dx                        ;每次读入一个字（16位）
         mov [ebx],ax                    ;存入目标缓冲区
         add ebx,2                       ;缓冲区指针前移2字节
         loop .readw                     ;循环256次，读完整个扇区

         pop edx
         pop ecx
         pop eax

         ret

;===============================================================================
;  子过程：make_gdt_descriptor —— 根据段基地址、界限和属性构造8字节的段描述符
;  输入：EAX=线性基地址
;        EBX=段界限（20位有效）
;        ECX=属性（各属性位在原始位置，其余位清零）
;  返回：EDX:EAX=完整的8字节描述符（高32位:低32位）
;
;  描述符格式（8字节）：
;    低32位(EAX)：[基地址15:0][段界限15:0]
;    高32位(EDX)：[基地址31:24][属性+界限19:16][基地址23:16]
;===============================================================================
make_gdt_descriptor:


         mov edx,eax
         shl eax,16                        ;EAX高16位 = 基地址低16位
         or ax,bx                          ;EAX低16位 = 段界限低16位 → 描述符低32位构造完毕

         and edx,0xffff0000                ;保留基地址高16位，清除低16位
         rol edx,8                         ;循环左移8位
         bswap edx                         ;字节序翻转 → 基地址31~24和23~16就位 (80486+)

         xor bx,bx			 ;清除EBX低16位，保留界限的高4位(bit19~16)
         or edx,ebx                        ;装配段界限的高4位(19:16)到EDX

         or edx,ecx                        ;合并属性位（G、DB、L、AVL、P、DPL、S、TYPE）

         ret
      
;===============================================================================
;  GDT伪描述符（6字节）：供lgdt指令加载
;    字[0]：GDT界限（表的字节数-1）
;    双字[2]：GDT的32位物理/线性基地址
;===============================================================================
         pgdt             dw 0
                          dd 0x00007e00      ;GDT的物理地址（位于0x7E00处）
;===============================================================================
;  MBR必须恰好512字节，以0x55AA结尾作为有效引导扇区标志
;===============================================================================
         times 510-($-$$) db 0              ;用0填充至510字节
                          db 0x55,0xaa      ;引导扇区签名（小端序：0xAA55）